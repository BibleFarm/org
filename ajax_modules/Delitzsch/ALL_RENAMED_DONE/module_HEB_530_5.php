<h1>אגרת שאול הראשונה אל הקורינתים<br>
פרק ה</h1>
<a name="aeb"> </a>
<h2>השליח מוכיח על חטא</h2>
<p>
<A NAME="1"> </A>
<B class="number">1</B>&nbsp;הַקּוֹל נִשְׁמַע בְּכָל-מָקוֹם שֶׁזְּנוּת בֵּינֵיכֶם וּזְנוּת אֲשֶׁר אֵין כָּמוֹהָ בַּגּוֹיִם עַד-שֶׁיִּקַּח אִישׁ אֶת-אֵשֶׁת אָבִיו׃(<a href="../../Tenach/Vayikra/Vayikra18.htm#8">ויקרא יח' ח'</a>)
<A NAME="2"> </A>
<B class="number">2</B>&nbsp;וְאַתֶּם מִתְגָּאִים תַּחַת אֲשֶׁר תִּתְאַבָּלוּ לְהָסִיר מִקִּרְבְּכֶם עֹשֵׂה הַמַּעֲשֶׂה הַזֶּה׃
<A NAME="3"> </A>
<B class="number">3</B>&nbsp;וְאָנֹכִי הָרָחוֹק מִכֶּם בְּגוּפִי וְקָרוֹב בְּרוּחִי כְּבָר דַּנְתִּי כְּאִלּוּ הָיִיתִי אֶצְלְכֶם עַל-הָאִישׁ אֲשֶׁר-עָשָׂה כַּדָּבָר הַזֶּה׃
<A NAME="4"> </A>
<B class="number">4</B>&nbsp;בְּשֵׁם אֲדֹנֵינוּ יֵשׁוּעַ הַמָּשִׁיחַ בְּהִקָּהֶלְכֶם יַחַד וְרוּחִי אִתְּכֶם עִם-גְּבוּרַת אֲדֹנֵינוּ יֵשׁוּעַ הַמָּשִׁיחַ׃
<A NAME="5"> </A>
<B class="number">5</B>&nbsp;לִמְסֹר אֶת-הָאִישׁ הַהוּא לַשָּׂטָן לְאַבֵּד אֶת-הַבָּשָׂר לְמַעַן יִוָּשַׁע הָרוּחַ בְּיוֹם הָאָדוֹן יֵשׁוּעַ׃
<A NAME="6"> </A>
<B class="number">6</B>&nbsp;לֹא-טוֹב הִתְהַלֶּלְכֶם הֲלֹא יְדַעְתֶּם כִּי מְעַט שְׂאֹר מְחַמֵּץ אֶת-כָּל-הָעִסָּה׃
<A NAME="7"> </A>
<B class="number">7</B>&nbsp;בַּעֲרוּ אֶת-הַשְּׂאוֹר הַיָּשָׁן (<a href="../../Tenach/Shemot/Shemot13.htm#7">שמות יג' ז'</a>) לְמַעַן תִּהְיוּ עִסָּה חֲדָשָׁה הֲלֹא לֶחֶם מַצּוֹת אַתֶּם כִּי גַּם-לָנוּ פִסְחֵנוּ הַנִּזְבָּח בַּעֲדֵנוּ הוּא הַמָּשִׁיחַ׃(<a href="../../Tenach/Shemot/Shemot12.htm#21">שמות יב' כא'</a>)
<A NAME="8"> </A>
<B class="number">8</B>&nbsp;עַל-כֵּן נָחֹגָּה-נָּא הֶחָג לֹא-בִשְׂאֹר יָשָׁן וְלֹא-בִשְׂאֹר רָעָה וָרֶשַׁע כִּי אִם-בְּמַצּוֹת הַתֹּם וְהָאֱמֶת׃
<A NAME="9"> </A>
<B class="number">9</B>&nbsp;כָּתַבְתִּי לָכֶם בָּאִגֶּרֶת שֶׁלּא תִתְעָרֲבוּ עִם-הַזֹּנִים׃
<A NAME="10"> </A>
<B class="number">10</B>&nbsp;וְאֵין-דַּעְתִּי עַל-הַזֹּנִים בָּעוֹלָם אוֹ עַל-בֹּצְעֵי בֶצַע וְגַזְלָנִים וְעֹבְדֵי אֱלִילִים כִּי אִם-כֵּן סוֹפְכֶם לָצֵאת מִן-הָעוֹלָם׃
<A NAME="11"> </A>
<B class="number">11</B>&nbsp;אַךְ-זֹאת כָּתַבְתִּי לָכֶם לְבִלְתִּי הִתְעָרֵב עִם-מִי שֶׁנִּקְרָא אָח וְהוּא זֹנֶה אוֹ-בֹצֵעַ בֶּצַע אוֹ-עֹבֵד אֱלִילִים אוֹ מְגַדֵּף אוֹ סֹבֵא אוֹ גַזְלָן וְאַף לֹא לֶאֱכֹל עִם-הָאִישׁ אֲשֶׁר כָּזֶה׃
<A NAME="12"> </A>
<B class="number">12</B>&nbsp;כִּי מַה-לִּי לִשְׁפֹּט אֶת-אֲשֶׁר בַּחוּץ הֲלֹא תִשְׁפְּטוּ אֵת אֲשֶׁר בַּבָּיִת׃
<A NAME="13"> </A>
<B class="number">13</B>&nbsp;וַאֲשֶׁר בַּחוּץ הָאֱלֹהִים יִשְׁפְּטֵם וְאַתֶּם תְּבַעֲרוּ אֶת-הָרַע מִקִּרְבְּכֶם׃
</p>
