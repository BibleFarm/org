<h1>אגרת אל העברים<br>
פרק א</h1>
<a name="aab"> </a>
<h2>אלהים דיבר באמצעות הבן</h2>
<p>
<A NAME="1"> </A>
<B class="number">1</B>&nbsp;הָאֱלֹהִים אֲשֶׁר-דִּבֶּר מִקֶּדֶם פְּעָמִים רַבּוֹת וּבְפָנִים שֹׁנִים אֶל-אֲבֹתֵינוּ בְּיַד הַנְּבִיאִים דִּבֶּר אֵלֵינוּ בְּאַחֲרִית הַיָּמִים הָאֵלֶּה בְּיַד בְּנוֹ׃(<a href="../../Tenach/Yirmiah/Yirmiah07.htm#25">ירמיהו ז' כה'</a>)
<A NAME="2"> </A>
<B class="number">2</B>&nbsp;אֲשֶׁר-שָׂמוֹ לְיוֹרֵשׁ כֹּל וְגַם-עָשָׂה בְיָדוֹ אֶת-הָעוֹלָמוֹת׃(<a href="../../Tenach/Tehillim/Tehillim02.htm#8">תהילים ב' ח'</a>)
<A NAME="3"> </A>
<B class="number">3</B>&nbsp;וְהוּא זֹהַר כְּבוֹדוֹ וְצֶלֶם עַצְמוּתוֹ וְנוֹשֵׂא כֹל בִּדְבַר גְּבוּרָתוֹ וְאַחֲרֵי טַהֲרוֹ אֹתָנוּ בְּנַפְשׁוֹ מֵחַטֹּאתֵינוּ יָשַׁב לִימִין הַגְּדֻלָּה בַּמְּרוֹמִים׃(<a href="../../Tenach/Tehillim/Tehillimb0.htm#1">תהילים קי' א'</a>)
<A NAME="4"> </A>
<B class="number">4</B>&nbsp;וַיִּגְדַּל מְאֹד מִן-הַמַּלְאָכִים כַּאֲשֶׁר הַשֵּׁם אֲשֶׁר נְחָלוֹ יָקָר הוּא מֵאֲשֶׁר לָהֶם׃
<A NAME="5"> </A>
<B class="number">5</B>&nbsp;כִּי אֶל-מִי מִן הַמַּלְאָכִים אָמַר מֵעוֹלָם בְּנִי אַתָּה אֲנִי הַיוֹם יְלִדְתִּיךָ (<a href="../../Tenach/Tehillim/Tehillim02.htm#7">תהילים ב' ז'</a>) וְעוֹד אֲנִי אֶהְיֶה-לּוֹ לְאָב וְהוּא יִהְיֶה-לִּי לְבֵן׃(<a href="../../Tenach/ShemuelB/ShemuelB07.htm#14">שמואל ב', ז' יד'</a>)
<A NAME="6"> </A>
<B class="number">6</B>&nbsp;וּבַהֲבִיאוֹ אֶת-הַבְּכוֹר שֵׁנִית לָעוֹלָם אֹמֵר וְהִשְׁתַּחֲווּ-לוֹ כָּל-אֱלֹהִים׃(<a href="../../Tenach/Tehillim/Tehillim97.htm#7">תהילים צז' ז'</a>)
<A NAME="7"> </A>
<B class="number">7</B>&nbsp;הֵן לַמַּלְאָכִים הוּא אֹמֵר עֹשֶׂה מַלְאָכָיו רוּחוֹת מְשָׁרֲתָיו אֵשׁ לֹהֵט׃(<a href="../../Tenach/Tehillim/Tehillima4.htm#4">תהילים קד' ד'</a>)
<A NAME="8"> </A>
<B class="number">8</B>&nbsp;וְלַבֵּן אֹמֵר כִּסְאֲךָ אֱלֹהִים עוֹלָם וָעֶד שֵׁבֶט מִישֹׁר שֵׁבֶט מַלְכוּתֶךָ׃
<A NAME="9"> </A>
<B class="number">9</B>&nbsp;אָהַבְתָּ צֶּדֶק וַתִּשְׂנָא רֶשַׁע עַל-כֵּן מְשָׁחֲךָ אֱלֹהִים אֱלֹהֶיךָ שֶׁמֶן שָׂשׂוֹן מֵחֲבֵרֶיךָ׃(<a href="../../Tenach/Tehillim/Tehillim45.htm#7">תהילים מה' ז'-ח'</a>)
<A NAME="10"> </A>
<B class="number">10</B>&nbsp;וְאֹמֵר אַתָּה אֲדֹנָי לְפָנִים הָאָרֶץ יָסַדְתָּ וּמַעֲשֵׂה יָדֶיךָ שָׁמָיִם׃
<A NAME="11"> </A>
<B class="number">11</B>&nbsp;הֵמָּה יֹאבֵדוּ וְאַתָּה תַעֲמֹד וְכֻלָּם כַּבֶּגֶד יִבְלוּ׃
<A NAME="12"> </A>
<B class="number">12</B>&nbsp;כַּלְּבוּשׁ תַּחֲלִיפֵם וְיַחֲלֹפוּ וְאַתָּה הוּא וּשְׁנוֹתֶיךָ לֹא יִתָּמּוּ׃(<a href="../../Tenach/Tehillim/Tehillima2.htm#26">תהילים קב' כו'-כח'</a>)
<A NAME="13"> </A>
<B class="number">13</B>&nbsp;וְאֶל-מִי מִן-הַמַּלְאָכִים אָמַר מֵעוֹלָם שֵׁב לִימִינִי עַד-אָשִׁית אֹיְבֶיךָ הֲדֹם לְרַגְלֶיךָ׃(<span class="x" onmousemove="Show('perush','הנ&quot;ל -- פסוק 3' ,event);" onmouseout="Hide();">***</span>)
<A NAME="14"> </A>
<B class="number">14</B>&nbsp;הֲלֹא כֻלָּם רוּחוֹת הַשָּׁרֵת הֵמָּה שְׁלוּחִים לְעֶזְרָה בְּעַד הַבָּאִים לָרֶשֶׁת אֶת-הַיְשׁוּעָה׃(<a href="../../Tenach/Tehillim/Tehillim91.htm#11">תהילים צא' יא'</a>)</p>
