<h1>אגרת שאול השניה אל הקורינתים<br>
פרק ו</h1>
<a name="afb"> </a>
<h2>שירות הריצוי -- המשך</h2>
<p>
<A NAME="1"> </A>
<B class="number">1</B>&nbsp;וַאֲנַחְנוּ כְּעֹזְרִים לוֹ נַזְהִירָה אֶתְכֶם שֶׁלּא תְקַבְּלוּ חֶסֶד אֱלֹהִים וְהָיָה לָרִיק׃
<A NAME="2"> </A>
<B class="number">2</B>&nbsp;כִּי הוּא אוֹמֵר בְּעֵת רָצוֹן עֲנִיתִיךָ וּבְיוֹם יְשׁוּעָה עֲזַרְתִּיךָ (<a href="../../Tenach/Yishayah/Yishayah49.htm#8">ישעיהו מט' ח'</a>) הִנֵּה עַתָּה עֵת רָצוֹן הִנֵּה עַתָּה יוֹם יְשׁוּעָה׃
<A NAME="3"> </A>
<B class="number">3</B>&nbsp;וְאֵין אֲנַחְנוּ נֹתְנִים מִכְשׁוֹל בִּמְאוּמָה פֶּן-יִהְיֶה שֵׁרוּתֵנוּ לִנְאָצָה׃
<A NAME="4"> </A>
<B class="number">4</B>&nbsp;כִּי אִם-נֵרָאֶה בְכָל-דָּבָר כִּמְשָׁרֲתֵי אֱלֹהִים בְּסַבְלָנוּת רַבָּה בְּלַחַץ וּבְצָרוֹת וּבִמְצוּקוֹת׃
<A NAME="5"> </A>
<B class="number">5</B>&nbsp;בְּמַכּוֹת וּבְמוֹסְרוֹת וּבִמְהוּמוֹת בִּתְלָאוֹת בִּשְׁקִידוֹת וּבְצוֹמוֹת׃
<A NAME="6"> </A>
<B class="number">6</B>&nbsp;בְּטָהֳרָה וּבְדַעַת וּבְאֹרֶךְ רוּחַ וּבְחֶסֶד וּבְרוּחַ הַקֹּדֶשׁ וּבְאַהֲבָה לֹא צְבוּעָה׃
<A NAME="7"> </A>
<B class="number">7</B>&nbsp;בִּדְבַר הָאֱמֶת וּבִגְבוּרַת אֱלֹהִים בִּכְלִי נֶשֶׁק הַצְּדָקָה מִיָּמִין וּמִשְּׂמֹאל׃
<A NAME="8"> </A>
<B class="number">8</B>&nbsp;בְּכָבוֹד וּבְקָלוֹן בְּשֵׁם-רַע וּבְשֵׁם-טוֹב כְּמַתְעִים וְנֶאֱמָנִים׃
<A NAME="9"> </A>
<B class="number">9</B>&nbsp;כְּאֵינָם יְדוּעִים וְגַם-יְדוּעִים כְּמֵתִים וְהִנְנוּ חַיִּים כִּמְיֻסָּרִים וְלֹא מוּמָתִים׃(<a href="../../Tenach/Tehillim/Tehillimb8.htm#18">תהילים קיח' יח'</a>)
<A NAME="10"> </A>
<B class="number">10</B>&nbsp;כְּנֶעֱצָבִים וּבְכָל-עֵת שְׂמֵחִים כְּרָשִׁים וּמַעֲשִׁירֵי רַבִּים כְּמִי שֶׁאֵין-לָהֶם-מְאוּמָה וְיֵשׁ לָהֶם כֹּל׃
<A NAME="11"> </A>
<B class="number">11</B>&nbsp;פִּינוּ פָּתוּחַ לָכֶם אַנְשֵׁי קוֹרִנְתּוֹס וְרָחַב לִבֵּנוּ׃
<A NAME="12"> </A>
<B class="number">12</B>&nbsp;לֹא-צַר לָכֶם הַמָּקוֹם בָּנוּ אַךְ-צַר הַמָּקוֹם בִּמְעֵיכֶם׃
<A NAME="13"> </A>
<B class="number">13</B>&nbsp;וְהָיָה זֶה גְּמוּלִי כְּדַבֵּר אֶל-בָּנִים אָנֹכִי מְדַבֵּר אִם-תַּרְחִיבוּ לְבַבְכֶם גַּם-אַתֶּם׃
</p>
<a name="afc"> </a>
<h2>היכל אלהים חיים</h2>
<p>
<A NAME="14"> </A>
<B class="number">14</B>&nbsp;אַל-תִּהְיוּ משְׁכִים בְּעֹל עִם-חַסְרֵי אֱמוּנָה (<a href="../../Tenach/Devarim/Devarim07.htm#2">דברים ז' ב'-ג'</a>;&nbsp;<a href="../../Tenach/Devarim/Devarim22.htm#10">כב' י'</a>) כִּי אֵי-זֹה חֶבְרָה לַצְּדָקָה עִם-הָעַוְלָה וּמַה-לָּאוֹר אֶת-הַחשֶׁךְ׃(<a href="../../Tenach/Bereshit/Bereshit01.htm#4">בראשית א' ד'</a>)
<A NAME="15"> </A>
<B class="number">15</B>&nbsp;אֵי-זֹה הַסְכָּמָה לַמָּשִׁיחַ עִם-בְּלִיַּעַל אוֹ מֶה חֵלֶק הַמַּאֲמִין עִם-שֶׁאֵינֶנּוּ מַאֲמִין׃
<A NAME="16"> </A>
<B class="number">16</B>&nbsp;וְאֵי-זֶה קֶשֶׁר יֵשׁ לְהֵיכַל אֱלֹהִים עִם-הָאֱלִילִים כִּי-אַתֶּם הֵיכַל אֱלֹהִים חַיִּים כְּמוֹ שֶׁאָמַר הָאֱלֹהִים וְשָׁכַנְתִּי וְהִתְהַלַּכְתִּי בְּתוֹכָם וְהָיִיתִי לָהֶם לֵאלֹהִים וְהֵם יִהְיוּ-לִי לָעָם׃(<a href="../../Tenach/Vayikra/Vayikra26.htm#12">ויקרא כו' יא'-יב'</a>;&nbsp;<a href="../../Tenach/Yechezkel/Yechezkel37.htm#27">יחזקאל לז' כז'</a>)
<A NAME="17"> </A>
<B class="number">17</B>&nbsp;עַל-כֵּן צְאוּ מִתּוֹכָם וְהִבָּרוּ נְאֻם-יְהוָֹה וְטָמֵא אַל-תִּגָּעוּ (<a href="../../Tenach/Yishayah/Yishayah52.htm#11">ישעיהו נב' יא'-יב'</a>) וַאֲנִי אֲקַבֵּץ אֶתְכֶם׃
<A NAME="18"> </A>
<B class="number">18</B>&nbsp;וְהָיִיתִי לָכֶם לְאָב וְאַתֶּם תִּהְיוּ-לִי לְבָנִים וּלְבָנוֹת נְאֻם-יְהוָֹה צְבָאוֹת׃(<a href="../../Tenach/ShemuelB/ShemuelB07.htm#14">שמואל ב', ז' יד'</a>;&nbsp;<a href="../../Tenach/Yirmiah/Yirmiah31.htm#8">ירמיהו לא' ח'</a>)
</p>
