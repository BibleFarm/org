<h1>התגלות<br>
פרק א</h1>
<p>
<A NAME="1"> </A>
<B class="number">1</B>&nbsp;חֲזוֹן יֵשׁוּעַ הַמָּשִׁיחַ אֲשֶׁר נָתַן-לוֹ הָאֱלֹהִים לְהַרְאוֹת אֶת-עֲבָדָיו אֵת אֲשֶׁר-יִהְיֶה בִּמְהֵרָה וְהוּא הוֹדִיעַ בְּשָׁלְחוֹ בְּיַד-מַלְאָכוֹ לְעַבְדּוֹ לְיוֹחָנָן׃(<a href="../../Tenach/Daniel/Daniel02.htm.org#28">דניאל ב' כח'</a>;&nbsp;<a href="../../Tenach/Daniel/Daniel08.htm#16">ח' טז'</a>)
<A NAME="2"> </A>
<B class="number">2</B>&nbsp;אֲשֶׁר הֵעִיד דְּבַר הָאֱלֹהִים וְעֵדוּת יֵשׁוּעַ הַמָּשִׁיחַ וְאֶת-כֹּל אֲשֶׁר רָאָה׃
<A NAME="3"> </A>
<B class="number">3</B>&nbsp;אַשְׁרֵי הַקּוֹרֵא וְאַשְׁרֵי הַשֹּׁמְעִים אֶת-דִּבְרֵי הַנְּבוּאָה וְשֹׁמְרִים אֶת-הַכָּתוּב בָּהּ כִּי קְרוֹבָה הָעֵת׃(<a href="../../Tenach/Tehillim/Tehillimb9.htm#2">תהילים קיט' ב'</a>)
<A NAME="4"> </A>
<B class="number">4</B>&nbsp;יוֹחָנָן לְשֶׁבַע הַקְּהִלּוֹת אֲשֶׁר בְּאַסְיָא חֶסֶד לָכֶם וְשָׁלוֹם מֵאֵת הַהוֶֹה וְהָיָה וְיָבוֹא וּמֵאֵת שִׁבְעַת הָרוּחוֹת אֲשֶׁר לִפְנֵי כִסְאוֹ׃
<A NAME="5"> </A>
<B class="number">5</B>&nbsp;וּמֵאֵת-יֵשׁוּעַ הַמָּשִׁיחַ הָעֵד הַנֶּאֱמָן (<a href="../../Tenach/Yishayah/Yishayah55.htm#4">ישעיהו נה' ד'</a>) וּבְכוֹר הַמֵּתִים וְעֶלְיוֹן לְמַלְכֵי-אָרֶץ (<a href="../../Tenach/Tehillim/Tehillim72.htm#11">תהילים עב' יא'</a>) אֲשֶׁר אָהַב אֹתָנוּ וּבְדָמוֹ [<span class="x" onmousemove="Show('perush','נוסח אחר&#58; רְחָצָנוּ' ,event);" onmouseout="Hide();">גְּאָלָנוּ</span>] מֵחַטֹּאתֵינוּ׃
<A NAME="6"> </A>
<B class="number">6</B>&nbsp;וַיַּעַשׂ אֹתָנוּ מְלָכִים וְכֹהֲנִים לֵאלֹהִים אָבִיו לוֹ הַכָּבוֹד וְהָעֹז לְעוֹלְמֵי עוֹלָמִים אָמֵן׃(<a href="../../Tenach/Yishayah/Yishayah61.htm#6">ישעיהו סא' ו'</a>)
<A NAME="7"> </A>
<B class="number">7</B>&nbsp;הִנֵּה הוּא בָא עִם-הָעֲנָנִים (<a href="../../Tenach/Daniel/Daniel07.htm#13">דניאל ז' יג'-יד'</a>) וְרָאֲתָה אֹתוֹ כָּל-עַיִן גַּם-הֵמָּה אֲשֶׁר דְּקָרֻהוּ וְסָפְדוּ עָלָיו כָּל-מִשְׁפְּחוֹת הָאָרֶץ כֵּן יִהְיֶה אָמֵן׃(<a href="../../Tenach/Zecharyah/Zecharyah12.htm#10">זכריה יב' י'-יד'</a>)
<A NAME="8"> </A>
<B class="number">8</B>&nbsp;אֲנִי הָאָלֶף וְהַתָּו [<span class="x" onmousemove="Show('perush','אין למילים שרש אף לא באחד מכתבי יד הישנים ואין ספק כי רק תוספת אחרונה הנה' ,event);" onmouseout="Hide();">רֹאשׁ וָסוֹף</span>] נְאֻם יְהוָֹה אֱלֹהִים הַהוֶֹה וְהָיָה וְיָבוֹא אֱלֹהֵי צְבָאוֹת׃(<a href="../../Tenach/Yishayah/Yishayah41.htm#4">ישעיהו מא' ד'</a>;&nbsp;<a href="../../Tenach/Yishayah/Yishayah44.htm#6">מד' ו'</a>;&nbsp;<a href="../../Tenach/Yishayah/Yishayah48.htm#12">מח' יב'</a>;&nbsp;<a href="../../Tenach/Shemot/Shemot03.htm#14">שמות ג' יד'</a>)
</p>
<a name="aab"> </a>
<h2>התגלות הקול המדבר</h2>
<p>
<A NAME="9"> </A>
<B class="number">9</B>&nbsp;אֲנִי יוֹחָנָן אֲחִיכֶם וְגַם-חָבֵר לָכֶם בְּלַחַץ וּבְמַלְכוּת וּבְסַבְלָנוּת לְמַעַן יֵשׁוּעַ הַמָּשִׁיחַ הָיִיתִי בָאִי אֲשֶׁר-שְׁמוֹ פַּטְמוֹס בַּעֲבוּר דְּבַר הָאֱלֹהִים וּבַעֲבוּר עֵדוּת יֵשׁוּעַ הַמָּשִׁיחַ׃
<A NAME="10"> </A>
<B class="number">10</B>&nbsp;וַתְּהִי עָלַי רוּחַ בְּיוֹם הָאָדוֹן וָאֶשְׁמַע אַחֲרַי קוֹל גָּדוֹל כְּקוֹל שׁוֹפָר׃(<a href="../../Tenach/Shemot/Shemot19.htm#19">שמות יט' יט'</a>;&nbsp;<a href="../../Tenach/Tehillim/Tehillim29.htm#4">תהילים כט' ד'</a>)
<A NAME="11"> </A>
<B class="number">11</B>&nbsp;וַיֹּאמֶר אֲנִי הָאָלֶף וַאֲנִי הַתָּו הָרִאשׁוֹן וְהָאַחֲרוֹן וְאֵת אֲשֶׁר אַתָּה רֹאֶה כְּתֹב עַל-סֵפֶר וּשְׁלָחֵהוּ אֶל-הַקְּהִלּוֹת אֲשֶׁר בְּאַסְיָא לְאֶפְסוֹס וְלִזְמִירְנָא וּלְפַרְגְּמוֹס וּלְתִיאֲטִירָא וּלְסַרְדִּיס וּלְפִילָדֶלְפִיָּא וּלְלוּדְקְיָא׃
<A NAME="12"> </A>
<B class="number">12</B>&nbsp;וָאֵפֶן לִרְאוֹת אֶת-הַקּוֹל הַמְדַבֵּר אֵלָי וַיְהִי בִּפְנוֹתִי וָאֵרֶא שֶׁבַע מְנֹרוֹת זָהָב׃(<a href="../../Tenach/Zecharyah/Zecharyah04.htm#2">זכריה ד' ב'</a>)
<A NAME="13"> </A>
<B class="number">13</B>&nbsp;וּבְתוֹךְ שֶׁבַע הַמְּנֹרוֹת דְּמוּת בֶּן-אָדָם (<a href="../../Tenach/Yechezkel/Yechezkel01.htm#26">יחזקאל א' כו'</a>;&nbsp;<a href="../../Tenach/Daniel/Daniel07.htm#13">דניאל ז' יג'</a>) לָבוּשׁ מְעִיל וְחָגוּר אֵזוֹר זָהָב עַל-לִבּוֹ׃
<A NAME="14"> </A>
<B class="number">14</B>&nbsp;וְרֹאשׁוֹ וּשְׂעָרוֹ לְבָנִים כְּצֶמֶר צַחַר כַּשָּׁלֶג וְעֵינָיו כְּלַבַּת אֵשׁ׃
<A NAME="15"> </A>
<B class="number">15</B>&nbsp;וּמַרְגְּלֹתָיו כְּעֵין נְחשֶׁת קָלָל צְרוּפוֹת בַּכּוּר (<a href="../../Tenach/Daniel/Daniel10.htm#6">דניאל י' ו'</a>;&nbsp;<a href="../../Tenach/Yechezkel/Yechezkel01.htm#27">יחזקאל א' כז'</a>) וְקוֹלוֹ כְּקוֹל מַיִם רַבִּים׃
<A NAME="16"> </A>
<B class="number">16</B>&nbsp;וּבְיַד-יְמִינוֹ שִׁבְעָה כוֹכָבִים וּמִפִּיו יוֹצֵאת חֶרֶב פִּיפִיּוֹת חַדָּה וּפָנָיו כַּשֶּׁמֶשׁ הַמֵּאִיר בִּגְבוּרָתוֹ׃
<A NAME="17"> </A>
<B class="number">17</B>&nbsp;וָאֵרֶא אוֹתוֹ וָאֶפֹּל לְרַגְלָיו כְּמֵת (<a href="../../Tenach/Daniel/Daniel08.htm#18">דניאל ח' יח'</a>;&nbsp;<a href="../../Tenach/Daniel/Daniel10.htm#8">י' ח'</a>) וַיָּשֶׁת עָלַי יַד-יְמִינוֹ וַיֹּאמֶר אַל-תִּירָא אֲנִי הָרִאשׁוֹן וְהָאַחֲרוֹן וְהֶחָי׃(<span class="x" onmousemove="Show('perush','הנ&quot;ל -- פסוק 8' ,event);" onmouseout="Hide();">***</span>)
<A NAME="18"> </A>
<B class="number">18</B>&nbsp;וָאֱהִי מֵת וְהִנְנִי חַי לְעוֹלְמֵי עוֹלָמִים [<span class="x" onmousemove="Show('perush','לא ברור אם המילה מנוסחה עיקרית, למרות שישנה בכתבי יד' ,event);" onmouseout="Hide();">אָמֵן</span>] וּבְיָדִי מַפְתְּחוֹת שְׁאוֹל וָמָוֶת׃
<A NAME="19"> </A>
<B class="number">19</B>&nbsp;וְעַתָּה כְתֹב אֵת אֲשֶׁר-רָאִיתָ וַאֲשֶׁר-נַעֲשֶׂה עַתָּה וַאֲשֶׁר-עָתִיד לִהְיוֹת אַחֲרֵי-כֵן׃
<A NAME="20"> </A>
<B class="number">20</B>&nbsp;אֶת-סוֹד שִׁבְעַת הַכּוֹכָבִים אֲשֶׁר רָאִיתָ בִימִינִי וְאֶת-שֶׁבַע מְנֹרוֹת הַזָּהָב שִׁבְעַת הַכּוֹכָבִים הֵם מַלְאֲכֵי שֶׁבַע הַקְּהִלּוֹת וְשֶׁבַע הַמְּנֹרוֹת אֲשֶׁר רָאִיתָ שֶׁבַע קְהִלּוֹת הֵנָּה׃
</p>
