<B class="number">43</B>&nbsp;שְׁמַעְתֶּם כִּי נֶאֱמַר וְאָהַבְתָּ לְרֵעֲךָ וְשָׂנֵאתָ אֶת-אֹיְבֶךָ׃(<a href="../../Tenach/Vayikra/Vayikra19.htm#18">ויקרא יט' יח'</a>)
<A NAME="44"> </A>
<B class="number">44</B>&nbsp;וַאֲנִי אֹמֵר לָכֶם אֶהֱבוּ אֶת-אֹיְבֵיכֶם [<span class="x" onmousemove="Show('perush','לא ברור אם המילים מנוסחה עיקרית, למרות שישנן בכתבי יד' ,event);" onmouseout="Hide();">בָּרֲכוּ אֶת-מְקַלְלֵיכֶם הֵיטִיבוּ לְשׂנְאֵיכֶם</span>] וְהִתְפַּלֲלוּ בְּעַד [<span class="x" onmousemove="Show('perush','לא ברור אם המילים מנוסחה עיקרית, למרות שישנן בכתבי יד' ,event);" onmouseout="Hide();">מַכְאִיבֵיכֶם וְ</span>]רֹדְפֵיכֶם׃
<A NAME="45"> </A>
<B class="number">45</B>&nbsp;לְמַעַן תִּהְיוּ בָנִים לַאֲבִיכֶם שֶׁבַּשָׁמָיִם אֲשֶׁר הוּא מַזְרִיחַ שִׁמְשׁוֹ לָרָעִים וְלַטּוֹבִים וּמַמְטִיר עַל-הַצַּדִּיקִים וְגַם עַל-הָרְשָׁעִים׃(<a href="../../Tenach/Tehillim/Tehillime5.htm#9">תהילים קמה' ט'</a>)
