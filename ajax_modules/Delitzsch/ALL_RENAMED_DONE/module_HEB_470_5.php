<B class="number">48</B>&nbsp;לָכֵן הֱיוּ שְׁלֵמִים כַּאֲשֶׁר אֲבִיכֶם שֶׁבַּשָׁמַיִם שָׁלֵם הוּא׃(<a href="../../Tenach/Vayikra/Vayikra19.htm#2">ויקרא יט' ב'</a>)
