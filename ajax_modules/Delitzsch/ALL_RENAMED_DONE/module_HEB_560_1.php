<h1>אגרת שאול אל האפסים<br>
פרק א</h1>
<p>
<A NAME="1"> </A>
<B class="number">1</B>&nbsp;פּוֹלוֹס שְׁלִיחַ יֵשׁוּעַ הַמָּשִׁיחַ בִּרְצוֹן אֱלֹהִים אֶל-הַקְּדשִׁים הַנִּמְצְאִים [<span class="x" onmousemove="Show('perush','לא ברור אם המילה מנוסחה עיקרית, למרות שישנה בכתבי יד' ,event);" onmouseout="Hide();">בְּאֶפְסוֹס</span>] וּמַאֲמִינִים בַּמָּשִׁיחַ יֵשׁוּעַ׃
<A NAME="2"> </A>
<B class="number">2</B>&nbsp;חֶסֶד לָכֶם וְשָׁלוֹם מֵאֵת הָאֱלֹהִים אָבִינוּ וַאֲדֹנֵינוּ יֵשׁוּעַ הַמָּשִׁיחַ׃
</p>
<a name="aab"> </a>
<h2>ברכות רוחניות במשיח</h2>
<p>
<A NAME="3"> </A>
<B class="number">3</B>&nbsp;בָּרוּךְ הָאֱלֹהִים וַאֲבִי אֲדֹנֵינוּ יֵשׁוּעַ הַמָּשִׁיחַ אֲשֶׁר בֵּרְכָנוּ בְּכָל-בִּרְכַּת רוּחַ בַּמְּרוֹמִים בַּמָּשִׁיחַ׃
<A NAME="4"> </A>
<B class="number">4</B>&nbsp;כַּאֲשֶׁר בָּחַר אֹתָנוּ בוֹ לִפְנֵי מוֹסְדוֹת תֵּבֵל לִהְיוֹת קְדשִׁים וּתְמִימִים לְפָנָיו בְּאַהֲבָה׃
<A NAME="5"> </A>
<B class="number">5</B>&nbsp;יְעָדָנוּ לוֹ לְבָנִים עַל-יְדֵי יֵשׁוּעַ הַמָּשִׁיחַ כִּרְצוֹן חֶפְצוֹ׃
<A NAME="6"> </A>
<B class="number">6</B>&nbsp;לִתְהִלַּת כְּבוֹד חַסְדּוֹ אֲשֶׁר-חָנַן אֹתָנוּ בִּידִידוֹ׃(<a href="../../Tenach/Tehillim/Tehillim84.htm#12">תהילים פד' יב'</a>)
<A NAME="7"> </A>
<B class="number">7</B>&nbsp;אֲשֶׁר-בּוֹ לָנוּ הַפִּדְיוֹם בְּדָמוֹ וּסְלִיחַת הַפְּשָׁעִים כְּרֹב חַסְדּוֹ׃(<a href="../../Tenach/Tehillim/Tehillima3.htm#3">תהילים קג' ג'</a>;&nbsp;<a href="../../Tenach/Tehillim/Tehillimd0.htm#4">קל' ד'</a>)
<A NAME="8"> </A>
<B class="number">8</B>&nbsp;אֲשֶׁר בָּא עָלֵינוּ בְּשֶׁפַע-חָכְמָה וְהַשְׂכֵּל׃
<A NAME="9"> </A>
<B class="number">9</B>&nbsp;וְהוֹדִיעַ אֹתָנוּ אֶת-סוֹד חֶפְצוֹ כִּרְצוֹנוֹ וַעֲצָתוֹ מֵרֹאשׁ אֲשֶׁר יָעַץ בּוֹ׃
<A NAME="10"> </A>
<B class="number">10</B>&nbsp;עַל-דְּבַר הַנְהָגָתוֹ בִּמְלֹאת הַיָּמִים לְקַבֵּל אֶת-הַכֹּל בַּמָּשִׁיחַ מַה-שֶּׁבַּשָּׁמַיִם וּמַה-שֶּׁבָּאָרֶץ׃
<A NAME="11"> </A>
<B class="number">11</B>&nbsp;וּבוֹ לָקַחְנוּ נַחֲלָתֵנוּ אֲנַחְנוּ הַמְיֻעָדִים לָהּ מִלְּפָנִים בְּמַחֲשֶׁבֶת פֹּעֵל הַכֹּל כַּעֲצַת חֶפְצוֹ׃(<a href="../../Tenach/Yishayah/Yishayah46.htm#10">ישעיהו מו' י'</a>)
<A NAME="12"> </A>
<B class="number">12</B>&nbsp;לִהְיוֹת לִתְהִלַּת כְּבוֹדוֹ אֲנַחְנוּ אֲשֶׁר יִחַלְנוּ אֶל-הַמָּשִׁיחַ מֵאָז׃
<A NAME="13"> </A>
<B class="number">13</B>&nbsp;וַאֲשֶׁר גַּם-אַתֶּם שְׁתוּלִים בּוֹ אַחֲרֵי שָׁמְעֲכֶם דְּבַר הָאֱמֶת אֶת-בְּשׂוֹרַת יְשׁוּעַתְכֶם וַאֲשֶׁר בּוֹ כְּשֶׁהֶאֱמַנְתֶּם גַּם-נֶחְתַּמְתֶּם בְּרוּחַ הַהַבְטָחָה רוּחַ הַקֹּדֶשׁ׃
<A NAME="14"> </A>
<B class="number">14</B>&nbsp;כִּי זֶה עֶרְבוֹן נַחֲלָתֵנוּ לִפְדּוֹת-לוֹ עַם סְגֻלָּה לִתְהִלַּת כְּבוֹדוֹ׃
</p>
<a name="aac"> </a>
<h2>תפילתו של השליח</h2>
<p>
<A NAME="15"> </A>
<B class="number">15</B>&nbsp;בַּעֲבוּר זֹאת גַּם-אָנֹכִי אַחֲרֵי שָׁמְעִי אֱמוּנַתְכֶם בַּאֲדֹנֵינוּ יֵשׁוּעַ וְהָאַהֲבָה אֲשֶׁר-אֲהַבְתֶּם אֶת-כָּל-הַקְּדשִׁים׃
<A NAME="16"> </A>
<B class="number">16</B>&nbsp;לֹא אֶחְדַּל מֵהוֹדוֹת בַּעֲבוּרְכֶם בְּהַזְכִּירִי אֶתְכֶם בִּתְפִלּתַי׃
<A NAME="17"> </A>
<B class="number">17</B>&nbsp;שֶׁיִּתֵּן לָכֶם אֱלֹהֵי אֲדֹנֵינוּ יֵשׁוּעַ הַמָּשִׁיחַ אֲבִי הַכָּבוֹד אֶת-רוּחַ הַחָכְמָה וְהֶחָזוֹן לָדַעַת אֹתוֹ׃
<A NAME="18"> </A>
<B class="number">18</B>&nbsp;וְיָאֵר עֵינֵי לְבַבְכֶם (<a href="../../Tenach/Tehillim/Tehillimb9.htm#18">תהילים קיט' יח'</a>) לְמַעַן תֵּדְעוּ מַה-הִיא תּוֹחֶלֶת קְרִיאָתוֹ וּמָה-עשֶׁר כְּבוֹד נַחֲלָתוֹ בַּקְּדשִׁים׃
<A NAME="19"> </A>
<B class="number">19</B>&nbsp;וּמַה-יִּתְרוֹן גְּדֻלַּת גְּבוּרָתוֹ בָּנוּ הַמַּאֲמִינִים כְּפִי פְעֻלַּת עֹצֶם כֹּחוֹ׃
<A NAME="20"> </A>
<B class="number">20</B>&nbsp;אֲשֶׁר פָּעַל בַּמָּשִׁיחַ בְּהָעִיר אֹתוֹ מִן-הַמֵּתִים וַיּוֹשִׁיבֶנּוּ לִימִינוֹ בַּמְּרוֹמִים׃(<a href="../../Tenach/Tehillim/Tehillimb0.htm#1">תהילים קי' א'</a>)
<A NAME="21"> </A>
<B class="number">21</B>&nbsp;מִמַּעַל לְכָל-שְׂרָרָה וְשָׁלְטָן וּגְבוּרָה וּמֶמְשָׁלָה וְכֹל הַנִּקְרָא בְשֵׁם לֹא-בָעוֹלָם הַזֶּה בִּלְבַד כִּי אִם-גַּם בָּעוֹלָם הַבָּא׃
<A NAME="22"> </A>
<B class="number">22</B>&nbsp;וַיָּשֶׁת כֹּל תַּחַת רַגְלָיו (<a href="../../Tenach/Tehillim/Tehillim08.htm#7">תהילים ח' ז'</a>//<span class="x" onmousemove="Show('perush','הנ&quot;ל -- פסוק 20' ,event);" onmouseout="Hide();">***</span>) וַיִּתֵּן אֹתוֹ לְרֹאשׁ לְכֹל אֶל-הָעֵדָה׃
<A NAME="23"> </A>
<B class="number">23</B>&nbsp;אֲשֶׁר הִיא גּוּפוֹ מְלוֹא שֶׁל-הַמְמַלֵּא אֶת-הַכֹּל בַּכֹּל׃
</p>
