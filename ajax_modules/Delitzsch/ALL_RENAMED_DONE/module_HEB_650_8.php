<h1>אגרת אל העברים<br>
פרק ח</h1>
<a name="ahb"> </a>
<h2>כהונתו הנעלה של ישוע</h2>
<p>
<A NAME="1"> </A>
<B class="number">1</B>&nbsp;וְזֶה רֹאשׁ הַנֶּאֱמָרִים שֶׁיֶּשׁ-לָנוּ כֹּהֵן גָּדוֹל אֲשֶׁר יָשַׁב לִימִין כִּסֵּא הַגְּדֻלָּה בַּשָּׁמָיִם׃(<a href="../../Tenach/Tehillim/Tehillimb0.htm#1">תהילים קי' א'</a>)
<A NAME="2"> </A>
<B class="number">2</B>&nbsp;וְהוּא מְשָׁרֵת הַקֹּדֶשׁ וְהַמִּשְׁכָּן הָאֲמִתִּי אֲשֶׁר-כּוֹנֲנוֹ אֲדֹנָי וְלֹא אָדָם׃
<A NAME="3"> </A>
<B class="number">3</B>&nbsp;כִּי כָּל-כֹּהֵן גָּדוֹל הוּא מֻפְקָד לְהַקְרִיב מְנָחוֹת וּזְבָחִים וְעַל-כֵּן צָרִיךְ שֶׁגַּם זֶה יִהְיֶה לוֹ מַה-שֶּׁיַּקְרִיב׃
<A NAME="4"> </A>
<B class="number">4</B>&nbsp;וְהִנֵּה אִלּוּ הָיָה בָאָרֶץ לֹא הָיָה כֹּהֵן כִּי יֶשׁ-פֹּה הַכֹּהֲנִים הַמַּקְרִיבִים הַקָּרְבָּנוֹת עַל-פִּי הַתּוֹרָה׃
<A NAME="5"> </A>
<B class="number">5</B>&nbsp;וּמְכַהֲנִים לִדְמוּת וְצֵל הַדְּבָרִים שֶׁבַּשָּׁמָיִם כַּאֲשֶׁר צֻוָּה משֶׁה בְּבֹאוֹ לְהָקִים אֶת-הַמִּשְׁכָּן כִּי-אָמַר אֵלָיו רְאֵה וַעֲשֵׂה הַכֹּל בְּתַבְנִיתוֹ אֲשֶׁר-אַתָּה מָרְאֶה בָּהָר׃(<a href="../../Tenach/Shemot/Shemot25.htm#40">שמות כה' מ'</a>)
<A NAME="6"> </A>
<B class="number">6</B>&nbsp;וְעַתָּה הוּא קִבֵּל שֵׁרוּת מְעֻלֶּה כְּפִי מַעֲלַת הַבְּרִית שֶׁהוּא סַרְסוֹר לָהּ אֲשֶׁר הוּקְמָה עַל-הַבְטָחוֹת טֹבוֹת וִיתֵרוֹת׃
<A NAME="7"> </A>
<B class="number">7</B>&nbsp;כִּי אִלוּ הָיְתָה הָרִאשׁוֹנָה הַהִיא גְּמוּרָה לֹא-יְבֻקַּשׁ מָקוֹם לִשְׁנִיָּה׃
<A NAME="8"> </A>
<B class="number">8</B>&nbsp;כִּי-כֹה אָמַר בְּהוֹכִיחַ אֹתָם הִנֵּה יָמִים בָּאִים נְאֻם-יְהוָֹה וְכָרַתִּי אֶת-בֵּית יִשְׂרָאֵל וְאֶת-בֵּית יְהוּדָה בְּרִית חֲדָשָׁה׃
<A NAME="9"> </A>
<B class="number">9</B>&nbsp;לֹא כַבְּרִית אֲשֶׁר כָּרַתִּי אֶת-אֲבוֹתָם בְּיוֹם הֶחֱזִיקִי בְיָדָם לְהוֹצִיאָם מֵאֶרֶץ מִצְרָיִם אֲשֶׁר-הֵמָּה הֵפֵרוּ אֶת-בְּרִיתִי וְאָנֹכִי <span class="x" onmousemove="Show('perush','בחלתי -- נ&quot;מ: בעלתי' ,event);" onmouseout="Hide();">בָּחַלְתִּי</span> בָם נְאֻם-יְהוָֹה׃
<A NAME="10"> </A>
<B class="number">10</B>&nbsp;כִּי זֹאת הַבְּרִית אֲשֶׁר אֶכְרֹת אֶת-בֵּית יִשְׂרָאֵל אַחֲרֵי הַיָּמִים הָהֵם נְאֻם-יְהוָֹה נָתַתִּי אֶת-תּוֹרָתִי בְּקִרְבָּם וְעַל-לִבָּם אֶכְתֳּבֶנָּה וְהָיִיתִי לָהֶם לֵאלֹהִים וְהֵמָּה יִהְיוּ-לִי לְעָם׃
<A NAME="11"> </A>
<B class="number">11</B>&nbsp;וְלֹא יְלַמְּדוּ עוֹד אִישׁ אֶת-רֵעֵהוּ וְאִישׁ אֶת-אָחִיו לֵאמֹר דְּעוּ אֶת-יְהוָֹה כִּי כוּלָּם יֵדְעוּ אוֹתִי לְמִקְּטַנָּם וְעַד-גְּדוֹלָם׃
<A NAME="12"> </A>
<B class="number">12</B>&nbsp;כִּי אֶסְלַח לַעֲוֹנָם וּלְחַטָּאתָם [<span class="x" onmousemove="Show('perush','לא ברור אם המילה מנוסחה עיקרית, למרות שישנה בכתבי יד' ,event);" onmouseout="Hide();">וּלְפִשְׁעֵיהֶם</span>] לֹא אֶזְכָּר-עוֹד׃(<a href="../../Tenach/Yirmiah/Yirmiah31.htm#30">ירמיהו לא' ל'-לג'</a>)
<A NAME="13"> </A>
<B class="number">13</B>&nbsp;הִנֵּה כְּשֶׁאָמַר בְּרִית חֲדָשָׁה יִשֵּׁן אֶת-הָרִאשׁוֹנָה וּמַה-שֶּׁהוּא נוֹשָׁן וּמַזְקִין קָרַב קִצּוֹ׃
</p>
