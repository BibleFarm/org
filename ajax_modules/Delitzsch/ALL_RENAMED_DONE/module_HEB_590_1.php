<h1>אגרת שאול הראשונה אל התסלוניקים<br>
פרק א</h1>
<p>
<A NAME="1"> </A>
<B class="number">1</B>&nbsp;פּוֹלוֹס וְסִלְוָנוֹס וְטִימוֹתִיּוֹס אֶל-קְהִלַּת הַתֶּסָּלוֹנִיקִיּים בֵּאלֹהִים הָאָב וּבָאָדוֹן יֵשׁוּעַ הַמָּשִׁיחַ חֶסֶד לָכֶם וְשָׁלוֹם מֵאֵת אֱלֹהִים אָבִינוּ וַאֲדֹנֵינוּ יֵשׁוּעַ הַמָּשִׁיחַ׃</p>
<a name="aab"> </a>
<h2>אמונת התסלוניקים בבשורה</h2>
<p>
<A NAME="2"> </A>
<B class="number">2</B>&nbsp;נוֹדֶה לֵאלֹהִים עַל-כֻּלְּכֶם בְּכָל-עֵת בְּהַזְכִּירֵנוּ אֶתְכֶם בִּתְפִלּוֹתֵינוּ׃
<A NAME="3"> </A>
<B class="number">3</B>&nbsp;בְּזָכְרֵנוּ תָמִיד לִפְנֵי הָאֱלֹהִים אָבִינוּ אֶת-פֹּעַל אֱמוּנַתְכֶם וִיגִיעַת אַהֲבַתְכֶם וְסַבְלָנוּת תִּקְוַתְכֶם לַאֲדֹנֵינוּ יֵשׁוּעַ הַמָּשִׁיחַ׃
<A NAME="4"> </A>
<B class="number">4</B>&nbsp;כִּי יָדַעְנוּ אַחַי חֲבִיבֵי הָאֱלֹהִים אֵת אֲשֶׁר נִבְחַרְתֶּם׃
<A NAME="5"> </A>
<B class="number">5</B>&nbsp;בַּאֲשֶׁר בְּשׂוֹרָתֵנוּ לֹא בָאָה לָכֶם בְּדִבּוּר לְבָד כִּי-גַם בִּגְבוּרָה וּבְרוּחַ הַקֹּדֶשׁ וּבְדַעַת נְכוֹנָה מְאֹד כַּאֲשֶׁר יְדַעְתֶּם אֵת אֲשֶׁר-הָיִינוּ בְּתוֹכֲכֶם לְמַעַנְכֶם׃
<A NAME="6"> </A>
<B class="number">6</B>&nbsp;וִהְיִיתֶם לְהֹלְכֵי בְעִקְּבוֹתֵינוּ וּבְעִקְּבוֹת אֲדֹנֵינוּ כִּי קִבַּלְתֶּם אֶת-הַדָּבָר בְּתוֹךְ עֳנִי רָב בְּחֶדְוַת רוּחַ הַקֹּדֶשׁ׃
<A NAME="7"> </A>
<B class="number">7</B>&nbsp;עַד-הֱיוֹתְכֶם לְמוֹפֵת לְכָל-הַמַּאֲמִינִים אֲשֶׁר בְּמַקְדּוֹנְיָא וּבַאֲכַיָּא׃
<A NAME="8"> </A>
<B class="number">8</B>&nbsp;כִּי מֵאִתְּכֶם נִשְׁמַע קוֹל דְּבַר יְהוָֹה לֹא בְּמַקְדּוֹנְיָא וַאֲכַיָּא לְבַד כִּי בְּכָל-מָקוֹם יָצְאָה אֱמוּנַתְכֶם לֵאלֹהִים וְאֵין-לָנוּ צֹרֶךְ לְדַבֵּר עוֹד דָּבָר׃
<A NAME="9"> </A>
<B class="number">9</B>&nbsp;כִּי-פִיהֶם הַמַּגִּיד מֶה-הָיָה מְבוֹאֵנוּ אֲלֵיכֶם וְאֵיךְ עֲזַבְתֶּם הָאֱלִילִים וְשַׁבְתֶּם לֵאלֹהִים לַעֲבֹד אֵל חַי וַאֲמִתִּי׃
<A NAME="10"> </A>
<B class="number">10</B>&nbsp;וּלְחַכּוֹת לִבְנוֹ מִן-הַשָּׁמַיִם אֲשֶׁר הֱעִירוֹ מִן-הַמֵּתִים לְיֵשׁוּעַ הַמַּצִּילֵנוּ מִן-הֶחָרוֹן הַבָּא׃
</p>
