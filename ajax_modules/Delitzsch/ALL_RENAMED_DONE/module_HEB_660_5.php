<h1>אגרת יעקב<br>
פרק ה</h1>
<a name="aeb"> </a>
<h2>אזהרה לעשירים</h2>
<p>
<A NAME="1"> </A>
<B class="number">1</B>&nbsp;הוֹי הָעֲשִׁירִים בְּכוּ וְהֵילִילוּ עַל-הַצָּרוֹת אֲשֶׁר תָּבֹאנָה עֲלֵיכֶם׃
<A NAME="2"> </A>
<B class="number">2</B>&nbsp;עָשְׁרְכֶם בָּלָה וּבִגְדֵיכֶם אֲכָלָם עָשׁ׃
<A NAME="3"> </A>
<B class="number">3</B>&nbsp;זְהַבְכֶם וְכַסְפְּכֶם כִּסָּתַם חֶלְאָה וְהָיְתָה חֶלְאָתָם בָּכֶם לְעֵדוּת וְאָכְלָה כְּמוֹ-אֵשׁ אֶת-בְּשַׂרְכֶם זֶה הוּא הָאוֹצָר אֲצַרְתֶּם לָכֶם לְקֵץ הַיָּמִים׃
<A NAME="4"> </A>
<B class="number">4</B>&nbsp;הִנֵּה שְׂכַר הַפֹּעֲלִים אֹסְפֵי קְצִיר שְׂדֹתֵיכֶם אֲשֶׁר עֲשַׁקְתֶּם צֹעֵק עֲלֵיכֶם וְצַעֲקַת הַקּוֹצְרִים בָּאָה בְאָזְנֵי יְהוָֹה צְבָאוֹת׃(<a href="../../Tenach/Vayikra/Vayikra19.htm#13">ויקרא יט' יג'</a>;&nbsp;<a href="../../Tenach/Devarim/Devarim24.htm#15">דברים כד' טו'</a>)
<A NAME="5"> </A>
<B class="number">5</B>&nbsp;הִתְעַדַּנְתֶּם בָּאָרֶץ וְהִתְעַנַּגְתֶּם וְהִשְׁמַנְתֶּם אֶת-לִבְּכֶם כְּמוֹ לְיוֹם טִבְחָה׃
<A NAME="6"> </A>
<B class="number">6</B>&nbsp;הִרְשַׁעְתֶּם וַהֲמִתֶּם אֶת-הַצַּדִּיק וְהוּא נַעֲנֶה לֹא-יִפְתַּח פִּיו׃(<a href="../../Tenach/Tehillim/Tehillim94.htm#21">תהילים צד' כא'</a>)
</p>
<a name="aec"> </a>
<h2>סבלנות ותפילה</h2>
<p>
<A NAME="7"> </A>
<B class="number">7</B>&nbsp;לָכֵן אַחַי דֹּמּוּ וְהוֹחִילוּ עַד-בּוֹא הָאָדוֹן הִנֵּה הָאִכָּר מְחַכֶּה לְטוּב תְּבוּאַת הָאֲדָמָה וּמְיַחֵל כִּי-יֵרֵד עָלֶיהָ גֶּשֶׁם יוֹרֶה וּמַלְקוֹשׁ׃(<a href="../../Tenach/Devarim/Devarim11.htm#14">דברים יא' יד'</a>)
<A NAME="8"> </A>
<B class="number">8</B>&nbsp;כֵּן הוֹחִילוּ גַם-אַתֶּם וְאַמְּצוּ לְבַבְכֶם כִּי קָרוֹב הָאָדוֹן לָבֹא׃
<A NAME="9"> </A>
<B class="number">9</B>&nbsp;אֶחָי אַל-תִּתְאוֹנֲנוּ אִישׁ עַל-רֵעֵהוּ פֶּן-תִּשָּׁפֵטוּ הִנֵּה הַדַּיָּן עֹמֵד בַּפָּתַח׃
<A NAME="10"> </A>
<B class="number">10</B>&nbsp;אֶחָי הַנְּבִיאִים אֲשֶׁר דִּבְּרוּ בְּשֵׁם יְהוָֹה הֵם יִהְיוּ לָכֶם לְמוֹפֵת הָעֳנִי וְהַתּוֹחֶלֶת׃(<a href="../../Tenach/Yirmiah/Yirmiah26.htm#14">ירמיהו כו' יד'-טז'</a>)
<A NAME="11"> </A>
<B class="number">11</B>&nbsp;הִנֵּה מְאַשְּׁרִים אֲנַחְנוּ אֶת-הַסֹּבְלִים שְׁמַעְתֶּם סַבְלָנוּת אִיּוֹב וְאֶת-אַחֲרִית הָאָדוֹן (<a href="../../Tenach/Iyov/Iyov42.htm#10">איוב מב' י'-יז'</a>) רְאִיתֶם כִּי-רַחוּם וְחַנּוּן יְהוָֹה׃(<a href="../../Tenach/Tehillim/Tehillima3.htm#13">תהילים קג' יג'</a>;&nbsp;<a href="../../Tenach/Eicha/Eicha03.htm#32">איכה ג' לב'</a>)
<A NAME="12"> </A>
<B class="number">12</B>&nbsp;וְרֹאשׁ דָּבָר אַחַי לֹא תִשָּׁבְעוּ לֹא בַשָּׁמַיִם וְלֹא בָאָרֶץ וְלֹא כָּל-שְׁבוּעָה אַחֶרֶת וִיהִי הֵן שֶׁלָּכֶם הֵן וְלֹא שֶׁלָּכֶם לֹא פֶּן-תִּפְּלוּ בִּידֵי הַדִּין׃
<A NAME="13"> </A>
<B class="number">13</B>&nbsp;אִישׁ מִכֶּם כִּי-יֵצַר לוֹ יִתְפַּלֵּל וַאֲשֶׁר יִיטַב לִבּוֹ הוּא יְזַמֵּר׃(<a href="../../Tenach/Tehillim/Tehillim50.htm#15">תהילים נ' טו'</a>;&nbsp;<a href="../../Tenach/Tehillim/Tehillim57.htm#8">נז' ח'</a>)
<A NAME="14"> </A>
<B class="number">14</B>&nbsp;אִישׁ מִכֶּם כִּי-יֶחֱלֶה יִקְרָא אֶת-זִקְנֵי הַקְּהִלָּה וְיִתְפַּלֲלוּ בַעֲדוֹ וִיסוּכֻהוּ שֶׁמֶן בְּשֵׁם יְהוָֹה׃
<A NAME="15"> </A>
<B class="number">15</B>&nbsp;וּתְפִלַּת הָאֱמוּנָה תּוֹשִׁיעַ אֶת-הַחוֹלֶה וַיְהוָֹה יְקִימֶנּוּ וַאֲשֶׁר חָטָא יִסָּלַח לוֹ׃(<a href="../../Tenach/Yishayah/Yishayah38.htm#17">ישעיהו לח' יז'</a>)
<A NAME="16"> </A>
<B class="number">16</B>&nbsp;הִתְוַדּוּ עֲוֹנוֹתֵיכֶם אִישׁ לִפְנֵי רֵעֵהוּ וְהִתְפַּלֲלוּ אִישׁ בְּעַד רֵעֵהוּ לְמַעַן תֵּרָפֵאוּ כִּי-גָדוֹל כֹּחַ תְּפִלַּת הַצַּדִּיק הַקֹּרֵא אֶל-אֱלֹהִים בְּחָזְקָה׃(<a href="../../Tenach/Mishley/Mishley28.htm#13">משלי כח' יג'</a>;&nbsp;<a href="../../Tenach/Bereshit/Bereshit20.htm#17">בראשית כ' יז'</a>)
<A NAME="17"> </A>
<B class="number">17</B>&nbsp;אֵלִיָּהוּ אֱנוֹשׁ אָנוּשׁ הָיָה כָמֹנוּ וְהִתְפַּלֵּל תְּפִלָּה שֶׁלּא יִהְיֶה מָטָר וְלֹא-הָיָה מָטָר בָּאָרֶץ שָׁלֹשׁ שָׁנִים וְשִׁשָּׁה חֳדָשִׁים׃
<A NAME="18"> </A>
<B class="number">18</B>&nbsp;וַיָּשָׁב וַיִּתְפַּלֵּל וְהַשָּׁמַיִם נָתְנוּ מָטָר וְהָאָרֶץ הִצְמִיחָה אֶת-פִּרְיָהּ׃(<a href="../../Tenach/MelachimA/MelachimA18.htm#42">מלכים א', יח' מב'-מה'</a>)
<A NAME="19"> </A>
<B class="number">19</B>&nbsp;אֶחָי כִּי יִתְעֶה אִישׁ בָּכֶם מִן-הָאֱמֶת וְאִישׁ אַחֵר יְשִׁיבֶנּוּ׃
<A NAME="20"> </A>
<B class="number">20</B>&nbsp;יֵדַע-נָא כִּי הַמֵּשִׁיב אֶת-הַחוֹטֵא מֵעִקְּשׁוּת דַּרְכּוֹ הוּא יוֹשִׁיעַ אֶת-נַפְשׁוֹ מִמָּוֶת וִיכַסֶּה עַל-הֲמוֹן פְּשָׁעִים׃(<a href="../../Tenach/Mishley/Mishley10.htm#12">משלי י' יב'</a>;&nbsp;<a href="../../Tenach/Yishayah/Yishayah55.htm#7">ישעיהו נה' ז'</a>)
</p>
