<h1>אגרת שאול השניה אל התסלוניקים<br>
פרק ב</h1>
<a name="abb"> </a>
<h2>התגלות איש הרשע</h2>
<p>
<A NAME="1"> </A>
<B class="number">1</B>&nbsp;וְהִנְנוּ מְבַקְשִׁים מִכֶּם אֶחָי עַל-אֹדוֹת בִּיאַת אֲדֹנֵינוּ יֵשׁוּעַ הַמָּשִׁיחַ וַאֲסִיפָתֵנוּ אֵלָיו׃ 
<A NAME="2"> </A>
<B class="number">2</B>&nbsp;אֲשֶׁר לֹא-תִטָּרֵף דַּעְתְּכֶם פִּתְאֹם וַאֲשֶׁר לֹא תִּבָּהֵלוּ לֹא בְרוּחַ וְלֹא בְדִבּוּר וְלֹא בְאִגֶּרֶת כְּאִלּוּ שְׁלוּחָה מֵאִתָּנוּ לֵאמֹר הִגִּיעַ יוֹם [<span class="x" onmousemove="Show('perush','נוסח אחר&#58; הַמָּשִׁיחַ' ,event);" onmouseout="Hide();">יְהוָֹה</span>]׃ 
<A NAME="3"> </A>
<B class="number">3</B>&nbsp;אַל-יַתְעֶה אֶתְכֶם אִישׁ בְּשׁוּם אוֹפֶן כִּי לֹא-יָבֹא בְּטֶרֶם יִהְיֶה בָרִאשׁוֹנָה הַמֶּרֶד וְנִגְלָה אִישׁ הָחַטָּאָה בֶּן-הָאֲבַדּוֹן׃ 
<A NAME="4"> </A>
<B class="number">4</B>&nbsp;הַמִּתְקוֹמֵם וְהַמִּתְרוֹמֵם עַל-כָּל-הַנִּקְרָא אֵל אוֹ עֲבוֹדָה כִּי גַם-יֵשֵׁב בְּהֵיכַל הָאֱלֹהִים כֵּאלֹהִים וּמַרְאֶה אֶת-עַצְמוֹ כִּי אֱלֹהִים הוּא׃(<a href="../../Tenach/Yishayah/Yishayah14.htm#14">ישעיהו יד' יד'</a>;&nbsp;<a href="../../Tenach/Yechezkel/Yechezkel28.htm#2">יחזקאל כח' ב'</a>) 
<A NAME="5"> </A>
<B class="number">5</B>&nbsp;הֲלֹא תִזְכְּרוּ כִּי-בְעוֹד הֱיוֹתִי אִתְּכֶם אֵלֶּה דִּבַּרְתִּי אֲלֵיכֶם׃ 
<A NAME="6"> </A>
<B class="number">6</B>&nbsp;וְעַתָּה יְדַעְתֶּם אֶת אֲשֶׁר יִמְנָעֵהוּ מִלְּהִגָּלוֹת בְּעִתּוֹ׃ 
<A NAME="7"> </A>
<B class="number">7</B>&nbsp;כִּי סוֹד הָרֶשַׁע כְּבָר הֵחֵלָּה פְעֻלָּתוֹ רַק שֶׁיּוּסַר מִתָּוֶךְ הַמֹּנֵעַ עַד-כֹּה׃ 
<A NAME="8"> </A>
<B class="number">8</B>&nbsp;וְאָז יִגָּלֶה הָרָשָׁע אֲשֶׁר יְמִיתֶנּוּ הָאָדוֹן בְּרוּחַ שְׂפָתָיו (<a href="../../Tenach/Iyov/Iyov04.htm#9">איוב ד' ט'</a>;&nbsp;<a href="../../Tenach/Yishayah/Yishayah11.htm#4">ישעיהו יא' ד'</a>) וְיַשְׁבִּיתֶנּוּ בְּהוֹפָעַת בִּיאָתוֹ׃ 
<A NAME="9"> </A>
<B class="number">9</B>&nbsp;אֵת אֲשֶׁר יָבוֹא כְּפִי פְּעֻלַּת הַשָׂטָן בְּרָב-כֹּחַ וּבְאֹתוֹת וּבְמוֹפְתֵי שָׁקֶר׃(<a href="../../Tenach/Shemot/Shemot07.htm#22">שמות ז' כב'</a>) 
<A NAME="10"> </A>
<B class="number">10</B>&nbsp;וּבְכָל-מִרְמַת אָוֶן בָּאֹבְדִים תַּחַת אֲשֶׁר לֹא קִבְּלוּ אֶת-אַהֲבַת הָאֱמֶת לְהִוָּשֵׁעַ׃ 
<A NAME="11"> </A>
<B class="number">11</B>&nbsp;וּבַעֲבוּר זֹאת יְשַׁלַּח לָהֶם הָאֱלֹהִים מַדּוּחֵי שָׁוְא לְהַאֲמִין בַּשָּׁקֶר׃(<a href="../../Tenach/MelachimA/MelachimA22.htm#22">מלכים א', כב' כב'</a>) 
<A NAME="12"> </A>
<B class="number">12</B>&nbsp;לְמַעַן יִדּוֹנוּ כָּל-אֲשֶׁר לֹא-הֶאֱמִינוּ בָאֱמֶת וְרָצוּ בָּעַוְלָה׃ 
</p>
