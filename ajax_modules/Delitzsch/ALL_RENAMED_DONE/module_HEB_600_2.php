<h2>נחמת המאמינים</h2>
<p>
<A NAME="13"> </A>
<B class="number">13</B>&nbsp;אֲבָל אֲנַחְנוּ חַיָּבִים לְהוֹדוֹת תָּמִיד לֵאלֹהִים בַּעֲבוּרְכֶם אַחַי חֲבִיבֵי הָאָדוֹן אֲשֶׁר מִבַּתְּחִלָּה בָּחַר בָּכֶם הָאֱלֹהִים לִישׁוּעָה בִּקְדֻשַּׁת הָרוּחַ וּבֶאֱמוּנַת הָאֱמֶת׃ 
<A NAME="14"> </A>
<B class="number">14</B>&nbsp;לָזֹאת קָרָא אֶתְכֶם בִּבְשׂוֹרָתֵנוּ לְנַחֲלַת כְּבוֹד אֲדֹנֵינוּ יֵשׁוּעַ הַמָּשִׁיחַ׃ 
<A NAME="15"> </A>
<B class="number">15</B>&nbsp;לָכֵן אַחַי עִמְדוּ וְהַחֲזִיקוּ בַקַּבָּלוֹת אֲשֶׁר לֻמַּדְתֶּם אִם בִּדְבָרֵנוּ אִם בְּאִגַּרְתֵּנוּ׃ 
<A NAME="16"> </A>
<B class="number">16</B>&nbsp;וַאֲדֹנֵינוּ יֵשׁוּעַ הַמָּשִׁיחַ וֵאלֹהֵינוּ אָבִינוּ אֲשֶׁר אָהַב אֹתָנוּ וַיִּתֶּן-לָנוּ בְחַסְדּוֹ נֶחָמַת עוֹלָם וְתִקְוָה טוֹבָה׃ 
<A NAME="17"> </A>
<B class="number">17</B>&nbsp;הוּא יְנַחֵם אֶת-לְבַבְכֶם וִיכוֹנֵן אֶתְכֶם בְּכָל-דָּבָר וּמַעֲשֶׂה טוֹב׃ 
</p>
