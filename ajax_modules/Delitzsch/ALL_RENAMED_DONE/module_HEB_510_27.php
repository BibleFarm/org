<h2>תלאות בים</h2>
<p>
<A NAME="13"> </A>
<B class="number">13</B>&nbsp;וְרוּחַ נשֶׁבֶת לְאַט מִן-הַדָּרוֹם וְהֵם חשְׁבִים שֶׁתִּצְלַח עֲצָתָם וַיַּעֲלוּ הָעוֹגִין וַיַּעַבְרוּ לְעֻמַּת שְׂפַת אִי קְרֵיטִי׃
<A NAME="14"> </A>
<B class="number">14</B>&nbsp;וְכִמְעַט אַחַר כֵּן וַתִּפְגַּע בָּאִי רוּחַ נַחֲשֹׁל אֲשֶׁר אַוְרִיקְלִידוֹן יִקָּרֵא לָהּ׃
<A NAME="15"> </A>
<B class="number">15</B>&nbsp;וַתְּטֹרַף הָאֳנִיָּה וְלֹא-יָכְלָה לַעֲמֹד נֶגֶד הָרוּחַ וַנֶּרֶף יָדֵינוּ מִמֶּנָּה וַנִּנָּדֵף׃
<A NAME="16"> </A>
<B class="number">16</B>&nbsp;וַתָּרָץ הַאֳנִיָּה אֶל-עֵבֶר אִי-קָטֹן קְלוֹדָה שְׁמוֹ וְכִמְעַט לֹא יָכֹלְנוּ לֶאֱחֹז הָעֲבָרָה׃
<A NAME="17"> </A>
<B class="number">17</B>&nbsp;וְאַחֲרֵי מָשְׁכָם אוֹתָהּ אֲלֵיהֶם הֵבִיאוּ לְעֶזְרָתָם כָּל-אֲשֶׁר הִשִּׂיגָה יָדָם וַיַּחְבְּשׁוּ אֶת-דָּפְנֵי הָאֳנִיָּה וּמִיִּרְאָתָם פֶּן-יִפְּלוּ אֶל-בֵּין רֻכְסֵי הַחוֹל הוֹרִידוּ אֶת הַקְּלָעִים וַנַּעֲבֹר הָלְאָה׃
<A NAME="18"> </A>
<B class="number">18</B>&nbsp;וַיְהִי הַסַּעַר הוֹלֵךְ וְסֹעֵר עָלֵינוּ וּמִמָּחֲרָת הֵטִילוּ אֶת-הַמַּשָּׂא אֶל-הַיָּם׃
<A NAME="19"> </A>
<B class="number">19</B>&nbsp;וּבַיּוֹם הַשְּׁלִישִׁי [<span class="x" onmousemove="Show('perush','נוסח אחר&#58; הֵטִילוּ' ,event);" onmouseout="Hide();">הֵטִילוּ בִידֵיהֶם</span>] אֶת-כְּלֵי הָאֳנִיָּה אֶל-הַיָּם׃
<A NAME="20"> </A>
<B class="number">20</B>&nbsp;וַיְהִי יָמִים רַבִּים גַּם-הַשֶּׁמֶשׁ גַּם-הַכּוֹכָבִים לֹא נִרְאוּ וְהַסְּעָרָה חָזְקָה עָלֵינוּ עַד-מְאֹד וַתִּכָּרֵת מִמֶּנּוּ כָּל-תִּקְוָה׃
<A NAME="21"> </A>
<B class="number">21</B>&nbsp;וּפוֹלוֹס עָמַד בְּתוֹכָם אַחֲרֵי הַאֲרִיכָם בַּצּוֹם וַיֹּאמַר אֲנָשִׁים לוּ שְׁמַעְתֶּם אֵלַי וְלֹא יְצָאתֶם מִקְּרֵיטִי כִּי אָז לֹא קָרָנוּ הַנֶּזֶק הַזֶּה בְּמֶרְיְכֶם׃
<A NAME="22"> </A>
<B class="number">22</B>&nbsp;וְעַתָּה אֲנִי אֹמֵר אֲלֵיכֶם חִזְקוּ וְאִמְצוּ כִּי לֹא-תֹאבַד נֶפֶשׁ מִכֶּם בִּלְתִּי הָאֳנִיָּה לְבַדָּה׃
<A NAME="23"> </A>
<B class="number">23</B>&nbsp;כִּי-בַלַּיְלָה הַזֶּה נִצַּב עָלַי מַלְאַךְ הָאֱלֹהִים אֲשֶׁר-לוֹ אָנֹכִי וַאֲשֶׁר אֲנִי עֹבֵד אֹתוֹ׃
<A NAME="24"> </A>
<B class="number">24</B>&nbsp;וַיֹּאמַר אַל-תִּירָא פוֹלוֹס עוֹד עָמוֹד תַּעֲמֹד לִפְנֵי קֵיסָר וְעַתָּה הִנֵּה נָתַן-לְךָ הָאֱלֹהִים אֶת-כָּל-הַהֹלְכִים אִתְּךָ בָּאֳנִיָּה׃
<A NAME="25"> </A>
<B class="number">25</B>&nbsp;עַל-כֵּן אֲנָשִׁים חִזְקוּ וְאִמְצוּ כִּי מַאֲמִין אֲנִי לֵאלֹהִים כִּי-כֵן יִהְיֶה כַּאֲשֶׁר נֶאֱמַר אֵלָי׃
<A NAME="26"> </A>
<B class="number">26</B>&nbsp;אַךְ הָשְׁלֵךְ נֻשְׁלַךְ אֶל-אַחַד הָאִיִּים׃
<A NAME="27"> </A>
<B class="number">27</B>&nbsp;וַיְהִי כְּבוֹא לֵיל אַרְבָּעָה עָשָׂר לְטִלְטוּלֵנוּ בְּיַם אַדְרִיָּה כַּחֲצוֹת הַלַּיְלָה וַיַּחְשְׁבוּ הַמַּלָּחִים כִּי-קָרְבָה לָהֶם יַבָּשֶׁת׃
<A NAME="28"> </A>
<B class="number">28</B>&nbsp;וַיּוֹרִידוּ אֶת-הָאֲנָךְ וַיִּמְצְאוּ עֹמֶק הַיָּם עֶשְׂרִים קוֹמָה וַיַּעַבְרוּ מְעַט וַיּוֹרִידוּ אֶת-הָאֲנָךְ שֵׁנִית וַיִּמְצְאוּ קוֹמוֹת חֲמֵשׁ עֶשְׂרֵה׃
<A NAME="29"> </A>
<B class="number">29</B>&nbsp;וַיִּירְאוּ פֶּן-יִפְגְּעוּ בְּשִׁנֵּי הַסְּלָעִים וַיַּשְׁלִיכוּ מֵעַל-אֲחוֹרֵי הָאֳנִיָּה אַרְבָּעָה עוֹגִינִים אֶל-הַיָּם וַיִּכְסְפוּ לְאוֹר הַיּוֹם׃
<A NAME="30"> </A>
<B class="number">30</B>&nbsp;וְהַמַּלָּחִים בִּקְשׁוּ לִבְרֹחַ מִן-הָאֳנִיָּה וַיּוֹרִידוּ אֶת-הָעֲבָרָה אֶל-הַיָּם בְּאָמְרָם כִּי יֵשׁ אֶת-נַפְשָׁם לְשַׁלֵּחַ עוֹגִינִים גַּם-מֵרֹאשׁ הָאֳנִיָּה׃
<A NAME="31"> </A>
<B class="number">31</B>&nbsp;וַיֹּאמֶר פּוֹלוֹס אֶל-שַׂר הַמֵּאָה וְאֶל-אַנְשֵׁי הַצָּבָא לֵאמֹר אִם-הֵם לֹא-יַעַמְדוּ עִתָּנוּ בָּאֳנִיָּה לֹא תוּכְלוּ אַתֶּם לְהִנָּצֵל׃
<A NAME="32"> </A>
<B class="number">32</B>&nbsp;וַיְקַצְּצוּ אַנְשֵׁי הַצָּבָא אֶת-חַבְלֵי הָעֲבָרָה וַיִּתְּנוּהָ לִנְפּוֹל׃
<A NAME="33"> </A>
<B class="number">33</B>&nbsp;וְהַבֹּקֶר טֶרֶם יֵאוֹר וַיְבַקֵּשׁ פּוֹלוֹס מִכֻּלָּם לִטְעָם-לֶחֶם לֵאמֹר הַיּוֹם יוֹם אַרְבָּעָה עָשָׂר אֲשֶׁר חִכִּיתֶם בַּצּוֹם וְלֹא טְעַמְתֶּם לָחֶם׃
<A NAME="34"> </A>
<B class="number">34</B>&nbsp;עַל-כֵּן קֹרֵא אֲנִי אֶתְכֶם לִטְעֹם לֶחֶם כִּי הוּא לִתְשׁוּעַתְכֶם כִּי אִישׁ מִכֶּם לֹא-יִפֹּל מִשַּׂעֲרַת רֹאשׁוֹ אָרְצָה׃
<A NAME="35"> </A>
<B class="number">35</B>&nbsp;הוּא דִבֶּר אֶת-הַדְּבָרִים הָאֵלֶּה וְהוּא לָקַח אֶת-הַלֶּחֶם וַיּוֹדֶה לֵאלֹהִים לִפְנֵי כֻלָּם וַיִּבְצַע וַיָּחֶל לֶאֱכֹל׃
<A NAME="36"> </A>
<B class="number">36</B>&nbsp;וַיֶּאֱמַץ לֵב כֻּלָם וַיִּטְעֲמוּ-אֹכֶל גַּם-הֵמָּה׃
<A NAME="37"> </A>
<B class="number">37</B>&nbsp;וַאֲנַחְנוּ כָּל-נֶפֶשׁ אֲשֶׁר בָּאֳנִיָּה מָאתַיִם וְשִׁבְעִים וָשֵׁשׁ׃
<A NAME="38"> </A>
<B class="number">38</B>&nbsp;וַיֹּאכְלוּ לְשָׂבְעָה וַיָּקֵלּוּ מֵעַל-הָאֳנִיָּה וַיָּטִילוּ אֶת-הַצֵּדָה אֶל-הַיָּם׃
</p>
