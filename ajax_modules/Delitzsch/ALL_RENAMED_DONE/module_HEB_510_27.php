<h2>היחלצות מן האניה</h2>
<p>
<A NAME="39"> </A>
<B class="number">39</B>&nbsp;הַבֹּקֶר אוֹר וְהֵם לֹא הִכִּירוּ אֶת-הָאָרֶץ אֲבָל רָאוּ כְּמִפְרַץ הַיָּם וְחוֹף לוֹ וַיִּוָּעֲצוּ לְנַהֵג אֵלָיו אֶת-הָאֳנִיָּה אִם-יוּכָלוּ׃
<A NAME="40"> </A>
<B class="number">40</B>&nbsp;וַיְגַדְּעוּ אֶת-הָעוֹגִינִים וַיַּעַזְבוּם לַיָּם וַיַּתִּירוּ גַּם-אֶת-מֵיתְרֵי הַמַּנְהִיג וַיִּפְרְשׂוּ מִפְרַשׂ הַתֹּרֶן אֶל-פְּנֵי הָרוּחַ וַיְבַקְשׁוּ לָבֹא אֶל-הַחוֹף׃
<A NAME="41"> </A>
<B class="number">41</B>&nbsp;וַיִּפְגְּעוּ בְמָקוֹם אֲשֶׁר הַיָּם מִשְּׁנֵי עֲבָרָיו וַתִּדְבַּק-בּוֹ הָאֳנִיָּה וַתַּעֲמֹד רֹאשָׁהּ לֹא יִנּוֹעַ וַאֲחוֹרֶיהָּ נִשְׁבְּרוּ מִשְּׁאוֹן הַגַּלִּים׃
<A NAME="42"> </A>
<B class="number">42</B>&nbsp;וַתְּהִי עֲצַת אַנְשֵׁי הַצָּבָא לְהָמִית אֶת-הָאֲסִירִים פֶּן-יִשְׂחֶה אִישׁ מֵהֶם וְנִמְלָט׃
<A NAME="43"> </A>
<B class="number">43</B>&nbsp;וְשַׂר הַמֵּאָה חָפֵץ לְהַצִּיל אֶת-פּוֹלוֹס וַיָּנֵא אֶת-עֲצָתָם וַיֹּאמַר הַיֹּדֵעַ לִשְׂחוֹת יֵרֵד וְיַעֲבֹר לַיַּבָּשָׁה בָּרִאשֹׁנָה וְהַנִּשְׁאָרִים אֵלֶּה עַל-קְרָשִׁים וְאֵלֶּה עַל-שִׁבְרֵי הָאֳנִיָּה׃
<A NAME="44"> </A>
<B class="number">44</B>&nbsp;וַיְהִי-כֵן וַיִּמָּלְטוּ כֻלָּם אֶל-הַיַּבָּשָׁה׃
