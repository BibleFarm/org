<h2>המסע לרומא</h2>
<p>
<A NAME="1"> </A>
<B class="number">1</B>&nbsp;וְכַאֲשֶׁר נִגְמַר דִּינֵנוּ לַעֲבֹר בָּאֳנִיָּה אֶל-אִיטַלְיָא מָסְרוּ אֶת-פּוֹלוֹס וּמִקְצָת אֲסִירִים אֲחֵרִים אֶל-שַׂר הַמֵּאָה לִגְדוּד אֲגוּסְטוֹס וּשְׁמוֹ יוּלִיּוֹס׃
<A NAME="2"> </A>
<B class="number">2</B>&nbsp;וַנֵּרֶד בָּאֳנִיָּה אַדְרָמִטִּית [<span class="x" onmousemove="Show('perush','נוסח אחר&#58; נְכוֹנִם' ,event);" onmouseout="Hide();">נְכוֹנָה</span>] לָלֶכֶת עַל-פְּנֵי חוֹף אַסְיָא וַנַּעֲבֹר הַיָּמָּה וַיְהִי אִתָּנוּ אֲרִסְטַרְכוֹס מַקְדּוֹן מִן-תַּסְלוֹנִיקִי׃
<A NAME="3"> </A>
<B class="number">3</B>&nbsp;וּמִמָּחֳרָת הִגַּעְנוּ לְצִידוֹן וְיוּלִיוֹס עָשָׂה חֶסֶד עִם-פּוֹלוֹס וַיַּנַּח לוֹ לָלֶכֶת אֶל-מְיֻדָּעָיו לְמַעַן יָשִׁיבוּ נַפְשׁו׃
<A NAME="4"> </A>
<B class="number">4</B>&nbsp;וַנֵּלֶךְ מִשָּׁם וַנַּעֲבֹר בִּסְבִיבוֹת קִיפְרוֹס כִּי הָרוּחוֹת הָיוּ לְנֶגְדֵּנוּ׃
<A NAME="5"> </A>
<B class="number">5</B>&nbsp;וַנַּעֲבֹר אֶת-הַיָּם אֲשֶׁר לִפְנֵי קִילִיקְיָא וּפַמְפּוּלְיָא וַנָּבֹא אֶל-מוּרָא אֲשֶׁר בְּלוּקְיָא׃
<A NAME="6"> </A>
<B class="number">6</B>&nbsp;וַיִּמְצָא-שָׁם שַׂר הַמֵּאָה אֳנִיָּה אֲלֶכְסַנְדְּרִית בָּאָה לְאִיטַלְיָא וַיַּעֲבִירֵנוּ אֶל-תּוֹכָהּ׃
<A NAME="7"> </A>
<B class="number">7</B>&nbsp;וְהָאֳנִיָּה הָלְכָה בִּכְבֵדוּת יָמִים רַבִּים וְאַחֲרֵי אֲשֶׁר יָגַעְנוּ וּבָאנוּ אֶל-מוּל קְנִידוֹס לֹא הִנִּיחָנוּ הָרוּחַ לַחְתֹּר אֶל-הַיַּבָּשָׁה וַנַּעֲבֹר מִצַּד קְרֵיטִי עַל-פְּנֵי הַר סַלְמוֹנִי׃
<A NAME="8"> </A>
<B class="number">8</B>&nbsp;וְאַחֲרֵי אֲשֶׁר עָבַרְנוּ בִּתְלָאוֹת בָּאנוּ לְמָקוֹם אֶחָד קָלִי לִימֶנֶס שְׁמוֹ וּקְרוֹבָה לוֹ עִיר ושְׁמָהּ לַסִּיָּא׃
<A NAME="9"> </A>
<B class="number">9</B>&nbsp;וַיְהִי מִקֵּץ יָמִים רַבִּים הִגִּיעָה עֵת הַסַּכָּנָה לְיֹרְדֵי הַיָּם כִּי גַם-הַצּוֹם כְּבָר עָבָר וַיַּזְהֵר אֹתָם פּוֹלוֹס׃
<A NAME="10"> </A>
<B class="number">10</B>&nbsp;וַיֹּאמֶר אֲלֵיהֶם אֲנָשִׁים רֹאֶה אֲנִי כִּי הֲלִיכָתֵנוּ תִּהְיֶה אַךְ-זָדוֹן וְנֶזֶק גָּדוֹל לֹא לַמַּשָּׂא וְלָאֳנִיָּה לְבַד כִּי אִם-גַּם לְנַפְשׁוֹתֵינוּ׃
<A NAME="11"> </A>
<B class="number">11</B>&nbsp;וְשַׂר הַמֵּאָה שָׁמַע אֶל-רַב הַחֹבֵל וְאֶל-בַּעַל הָאֳנִיָּה וְאֶל-דִּבְרֵי פוֹלוֹס לֹא שָׁמָע׃
<A NAME="12"> </A>
<B class="number">12</B>&nbsp;וּבַאֲשֶׁר הַחוֹף לֹא טוֹב לַעֲמָד-בּוֹ בִּימֵי הַסְּתָו יָעֲצוּ הָרַבִּים לַעֲבֹר מִשָּׁם כִּי אָמְרוּ אוּלַי נוּכַל הַגִּיעַ לְפֵינִיכַס וְיָשַׁבְנוּ שָׁם בִּימֵי הַסְּתָו וְהוּא חוֹף בִּקְרֵיטִי פּוֹנֶה לִפְאַת דָּרוֹם מַעֲרָבָה וְלִפְאַת צָפוֹן מַעֲרָבָה׃
</p>
