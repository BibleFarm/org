<h1>אגרת שאול השניה אל הקורינתים<br>
פרק ג</h1>
<a name="acb"> </a>
<h2>משרתי הברית החדשה</h2>
<p>
<A NAME="1"> </A>
<B class="number">1</B>&nbsp;הֲנָחֵל עוֹד לְשַׁבֵּחַ עַצְמֵנוּ הַאִם-צְרִיכִים אֲנַחְנוּ כַּאֲחֵרִים לְאִגְּרוֹת אֲלֵיכֶם אוֹ מִכֶּם מַזְכִּירוֹת אֹתָנוּ לָשֶׁבַח׃
<A NAME="2"> </A>
<B class="number">2</B>&nbsp;אַתֶּם אִגַּרְתֵּנוּ הַכְּתוּבָה בִּלְבָבֵנוּ וְנוֹדָעָה וְנִקְרָאָה לְכָל-אָדָם׃
<A NAME="3"> </A>
<B class="number">3</B>&nbsp;כִּי בְיָדוּעַ שֶׁאַתֶּם אִגֶּרֶת הַמָּשִׁיחַ עֲרוּכָה עַל-יְדֵי שֵׁרוּתֵנוּ כְּתוּבָה לֹא בִדְיוֹ כִּי אִם-בְּרוּחַ אֱלֹהִים חַיִּים וְלֹא עַל-לוּחוֹת אֶבֶן (<a href="../../Tenach/Shemot/Shemot24.htm#12">שמות כד' יב'</a>) כִּי אִם-עַל-לוּחוֹת בָּשָׂר לוּחוֹת הַלֵּב׃(<a href="../../Tenach/Yechezkel/Yechezkel11.htm#19">יחזקאל יא' יט'</a>;&nbsp;<a href="../../Tenach/Yechezkel/Yechezkel36.htm#26">לו' כו'</a>) 
<A NAME="4"> </A>
<B class="number">4</B>&nbsp;וְזֶה בִטְחוֹנֵנוּ עַל-יְדֵי הַמָּשִׁיחַ לִפְנֵי הָאֱלֹהִים׃ 
<A NAME="5"> </A>
<B class="number">5</B>&nbsp;לֹא-שֶׁכְּשֵׁרִים אֲנַחְנוּ מֵעַצְמֵנוּ לָדִין כִּי כִשְׁרוֹנֵנוּ מֵאֵת הָאֱלֹהִים׃ 
<A NAME="6"> </A>
<B class="number">6</B>&nbsp;וְהוּא הִכְשִׁיר אֹתָנוּ לִמְשָׁרֲתֵי בְּרִית חֲדָשָׁה לֹא שֶׁל-הָאוֹתִיּוֹת אֶלָּא שֶׁל-הָרוּחַ כִּי-הָאוֹת תָּמִית וְהָרוּחַ יְחַיֶּה׃(<a href="../../Tenach/Yirmiah/Yirmiah31.htm#30">ירמיהו לא' ל'</a>)
<A NAME="7"> </A>
<B class="number">7</B>&nbsp;וְאִם-שֵׁרוּת הַמָּוֶת הֶחָרוּת בְּאוֹתִיּוֹת עַל-הָאֶבֶן (<a href="../../Tenach/Devarim/Devarim10.htm#4">דברים י' ד'</a>) נִרְאָה בְּכָבוֹד עַד-שֶׁלּא יָכְלוּ בְּנֵי יִשְׂרָאֵל לְהַבִּיט אֶל-פְּנֵי משֶׁה מִפְּנֵי כְּבוֹד פָּנָיו הַבָּטֵל׃(<a href="../../Tenach/Shemot/Shemot34.htm#29">שמות לד' כט'-לה'</a>)
<A NAME="8"> </A>
<B class="number">8</B>&nbsp;כְּבוֹד שֵׁרוּת הָרוּחַ כַּמָּה יִגְדַּל כְּבוֹדוֹ׃
<A NAME="9"> </A>
<B class="number">9</B>&nbsp;כִּי אִם-הַשֵּׁרוּת אֲשֶׁר לְחַיֵּב כָּבוֹד הוּא הַשֵּׁרוּת אֲשֶׁר לְזַכּוֹת כַּמָּה יֶעְדַּף בְּכָבוֹד׃
<A NAME="10"> </A>
<B class="number">10</B>&nbsp;כִּי אַף-מַה-שֶּׁנִּתַּן בְּכָבוֹד כַּמִּדָּה הַהִיא כְּבוֹדוֹ כְאַיִן לְעֻמַּת הַכָּבוֹד הַנַּעֲלֶה הַזֶּה׃
<A NAME="11"> </A>
<B class="number">11</B>&nbsp;כִּי אִם-הַדָּבָר הַבָּטֵל יֶשׁ-לוֹ חֵלֶק בְּכָבוֹד הַדָּבָר הַקַּיָּם עַל-אַחַת כַּמָּה וְכַמָּה׃
<A NAME="12"> </A>
<B class="number">12</B>&nbsp;עַל-כֵּן בִּהְיוֹת לָנוּ תִּקְוָה כַּתִּקְוָה הַזֹּאת פִּתְחוֹן פֶּה רַב לָנוּ׃
<A NAME="13"> </A>
<B class="number">13</B>&nbsp;וְלֹא כְמשֶׁה אֲשֶׁר נָתַן מַסְוֶה עַל-פָּנָיו פֶּן-יַבִּיטוּ בְּנֵי יִשְׂרָאֵל אֶל-סוֹף מַה-שֶּׁסּוֹפוֹ לְהִבָּטֵל׃(<span class="x" onmousemove="Show('perush','הנ&quot;ל פסוק 7' ,event);" onmouseout="Hide();">***</span>) 
<A NAME="14"> </A>
<B class="number">14</B>&nbsp;אֲבָל נִטַּמְטְמוּ דֵעוֹתֵיהֶם (<a href="../../Tenach/Yishayah/Yishayah06.htm#10">ישעיהו ו' י'</a>) כִּי עַד-הַיּוֹם הַזֶּה בְּקָרְאָם הַבְּרִית הַיְשָׁנָה נִשְׁאָר הַמַּסְוֶה הַהוּא וְלֹא-גֻלָּה עָלֶיהָ כִּי לֹא יוּסַר כִּי אִם-בַּמָּשִׁיחַ׃
<A NAME="15"> </A>
<B class="number">15</B>&nbsp;עַד-הַיּוֹם הַזֶּה הֵם קֹרְאִים אֶת-משֶׁה וְהַמַּסְוֶה מֻנָּח עַל-לִבָּם׃
<A NAME="16"> </A>
<B class="number">16</B>&nbsp;אַךְ בְּעֵת שׁוּבָם אֶל-הָאָדוֹן יוּסַר הַמַּסְוֶה׃
<A NAME="17"> </A>
<B class="number">17</B>&nbsp;הָאָדוֹן הוּא הָרוּחַ וּבַאֲשֶׁר רוּחַ הָאָדוֹן שָׁם חֵרוּת׃
<A NAME="18"> </A>
<B class="number">18</B>&nbsp;וַאֲנַחְנוּ כֻלָּנוּ <span class="x" onmousemove="Show('perush','רואים -- או: משקפים' ,event);" onmouseout="Hide();">רֹאִים</span> בַּמַּרְאָה (<a href="../../Tenach/Shemot/Shemot34.htm#34">שמות לד' לד'</a>) אֶת-כְּבוֹד הָאָדוֹן וּפָנֵינוּ מְגֻלִּים וְנִתְהַפֵּךְ לְעֶצֶם דְּמוּת הַהִיא מִכָּבוֹד לְכָבוֹד כְּמִי שֶׁמִּתְחַלֵּף מֵאֵת אֲדוֹן הָרוּחַ׃
</p>
