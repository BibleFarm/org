<A NAME="6"> </A>
<B class="number">6</B>&nbsp;אֵינֶנּוּ פֹה כִּי קָם זְכֹרְנָה אֵת אֲשֶׁר-דִּבֶּר אֲלֵיכֶן בְּעוֹדֶנּוּ בַגָּלִיל לֵאמֹר׃
<A NAME="7"> </A>
<B class="number">7</B>&nbsp;כִּי צָרִיךְ בֶּן-הָאָדָם לְהִמָּסֵר לִידֵי אֲנָשִׁים חַטָּאִים וּלְהִצָּלֵב וּבַיּוֹם הַשְּׁלִישִׁי קוֹם יָקוּם׃
<A NAME="8"> </A>
<B class="number">8</B>&nbsp;וַתִּזְכֹּרְנָה אֶת-דְּבָרָיו׃
<A NAME="9"> </A>
<B class="number">9</B>&nbsp;וַתָּשֹׁבְנָה מִן-הַקָּבֶר וַתַּגֵּדְנָה אֵת כָּל-הַדְּבָרִים הָאֵלֶּה לְעַשְׁתֵּי הֶעָשָׂר וּלְכָל-הַנִּשְׁאָרִים׃
