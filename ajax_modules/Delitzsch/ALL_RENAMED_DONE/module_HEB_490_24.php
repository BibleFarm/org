<h2>ישוע בדרך לעמאוס</h2>
<h3>(<a href="../Mark/Mark16.htm#apca">מרקוס טז' 13-12</a>)</h3>
<p>
<A NAME="13"> </A>
<B class="number">13</B>&nbsp;וְהִנֵּה שְׁנַיִם מֵהֶם הָיוּ הֹלְכִים בְּעֶצֶם הַיּוֹם הַזֶּה אֶל-כְּפָר אֶחָד הַרְחֵק מִירוּשָׁלַיִם כְּ[<span class="x" onmousemove="Show('perush','המילה אינה בנוסחה הנהוגה אך נמצאת בספרים כתבי יד ישנים' ,event);" onmouseout="Hide();">מֵאָה וְ</span>]שִׁשִּׁים רִיס וּשְׁמוֹ עַמָּאוּס׃
<A NAME="14"> </A>
<B class="number">14</B>&nbsp;וְהֵם נִדְבְּרוּ אִישׁ אֶל-רֵעֵהוּ עַל-כָּל-הַקֹּרוֹת הָאֵלֶּה׃
<A NAME="15"> </A>
<B class="number">15</B>&nbsp;וַיְהִי הֵם מְדַבְּרִים וּמִתְוַכְּחִים יָחַד וְהִנֵּה יֵשׁוּעַ עַצְמוֹ נִגַּשׁ וַיֵּלֶךְ אִתָּם׃
<A NAME="16"> </A>
<B class="number">16</B>&nbsp;וְעֵינֵיהֶם אֲחוּזוֹת וְלֹא יַכִּירֻהוּ׃
<A NAME="17"> </A>
<B class="number">17</B>&nbsp;וַיֹּאמֶר אֲלֵיהֶם מָה הַדְּבָרִים הָאֵלֶּה אֲשֶׁר אַתֶּם נֹשְׂאִים וְנֹתְנִים בָּהֶם יַחְדָּו בַּדָּרֶךְ וַיַּעַמְדוּ וּפְנֵיהֶם זֹעֲפִים׃
<A NAME="18"> </A>
<B class="number">18</B>&nbsp;וַיַּעַן אֶחָד מֵהֶם וּשְׁמוֹ קְלֵיוֹפָס וַיֹּאמֶר אֵלָיו הַאַתָּה לְבַדְּךָ גָּר בִּירוּשָׁלַיִם וְלֹא יָדַעְתָּ אֶת-אֲשֶׁר נַעֲשָׂה בְתוֹכָהּ בַּיָּמִים הָאֵלֶּה׃
<A NAME="19"> </A>
<B class="number">19</B>&nbsp;וַיֹּאמֶר אֲלֵיהֶם וּמָה הִיא וַיַּגִּידוּ אֵלָיו מַעֲשֵׂה יֵשׁוּעַ הַנָּצְרִי אֲשֶׁר הָיָה אִישׁ נָבִיא גִּבּוֹר בְּפֹעַל וּבְאֹמֶר לִפְנֵי הָאֱלֹהִים וְלִפְנֵי כָּל-הָעָם׃
<A NAME="20"> </A>
<B class="number">20</B>&nbsp;וְכֹהֲנֵינוּ הַגְּדוֹלִים וּזְקֵנֵינוּ הִסְגִּירֻהוּ לְמִשְׁפַּט-מָוֶת וַיִּצְלְבֻהוּ׃
<A NAME="21"> </A>
<B class="number">21</B>&nbsp;וַאֲנַחְנוּ קִוִּינוּ כִּי זֶה-הוּא הֶעָתִיד לִגְאֹל אֶת-יִשְׂרָאֵל וְעַתָּה זֶה שְׁלשֶׁת יָמִים הַיּוֹם מֵאָז נַעֲשׂוּ אֵלֶּה׃
<A NAME="22"> </A>
<B class="number">22</B>&nbsp;אֶלָּא שֶׁגַּם-נָשִׁים מִקִּרְבֵּנוּ הֶחֱרִידֻנוּ אֲשֶׁר-קִדְּמוּ בַבֹּקֶר לָבֹא אֶל-הַקָּבֶר׃
<A NAME="23"> </A>
<B class="number">23</B>&nbsp;וְלֹא מָצְאוּ אֶת-גְּוִיָּתוֹ וַתָּבֹאנָה וַתַּגֵּדְנָה כִּי רָאוּ גַּם-מַרְאֵה מַלְאָכִים הָאֹמְרִים כִּי-הוּא חָי׃
<A NAME="24"> </A>
<B class="number">24</B>&nbsp;וַיֵּלְכוּ אֲנָשִׁים מִשֶּׁלָּנוּ אֶל-הַקָּבֶר וַיִּמְצְאוּ כַּאֲשֶׁר אָמְרוּ הַנָּשִׁים וְאוֹתוֹ לֹא רָאוּ׃
<A NAME="25"> </A>
<B class="number">25</B>&nbsp;וַיֹּאמֶר אֲלֵיהֶם הוֹי חַסְרֵי דַעַת וְכִבְדֵי לֵב מֵהַאֲמִין בְּכֹל אֲשֶׁר-דִּבְּרוּ הַנְּבִיאִים׃(<a href="../../Tenach/Tehillim/Tehillim40.htm#8">תהילים מ' ח'</a>)
<A NAME="26"> </A>
<B class="number">26</B>&nbsp;הֲלֹא עַל-הַמָּשִׁיחַ הָיָה לִסְבֹּל אֶת-כָּל-זֹאת וּלְהִכָּנֵס בִּכְבוֹדוֹ׃
<A NAME="27"> </A>
<B class="number">27</B>&nbsp;וַיָּחֶל מִמּשֶׁה וּמִכָּל-הַנְּבִיאִים וַיְבַאֵר לָהֶם אֶת-כָּל-הַכְּתוּבִים הַנֶּאֱמָרִים עָלָיו׃
<A NAME="28"> </A>
<B class="number">28</B>&nbsp;וַיִּקְרְבוּ אֶל-הַכְּפָר אֲשֶׁר-הֵם הֹלְכִים שָׁמָּה וַיָּשֶׂם פָּנָיו וַיְהִי כְּהֹלֵךְ לוֹ לְדַרְכּוֹ׃
<A NAME="29"> </A>
<B class="number">29</B>&nbsp;וַיִּפְצְרוּ-בוֹ לֵאמֹר שְׁבָה אִתָּנוּ כִּי עֶת-עֶרֶב הִגִּיעַ וְנָטָה הַיּוֹם וַיָּבֹא הַבַּיְתָה לָשֶׁבֶת אִתָּם׃
<A NAME="30"> </A>
<B class="number">30</B>&nbsp;וַיְהִי כַּאֲשֶׁר הֵסֵב עִמָּהֶם וַיִּקַּח אֶת-הַלָּחֶם וַיְבָרֶךְ וַיִּבְצַע וַיִּתֵּן לָהֶם׃
<A NAME="31"> </A>
<B class="number">31</B>&nbsp;וַתִּפָּקַחְנָה עֵינֵיהֶם וַיַּכִּירֻהוּ וְהוּא חָמַק עָבַר מֵעֵינֵיהֶם׃
<A NAME="32"> </A>
<B class="number">32</B>&nbsp;וַיֹּאמְרוּ אִישׁ אֶל-רֵעֵהוּ הֲלֹא בֹעֵר הָיָה לְבָבֵנוּ בְּקִרְבֵּנוּ בְּדַבְּרוֹ אֵלֵינוּ בַּדֶּרֶךְ וַיִּפְתַּר-לָנוּ אֵת הַכְּתוּבִים׃
<A NAME="33"> </A>
<B class="number">33</B>&nbsp;וַיָּקוּמוּ בַּשָּׁעָה הַהִיא וַיָּשׁוּבוּ יְרוּשָׁלָיִם וַיִּמְצְאוּ אֶת-עַשְׁתֵּי הֶעָשָׂר וְאֶת-אֲשֶׁר אִתָּם וְהֵם נִקְהָלִים יָחַד׃
<A NAME="34"> </A>
<B class="number">34</B>&nbsp;וְאֹמְרִים אָכֵן קָם הָאָדוֹן מִן-הַמֵּתִים וְנִרְאָה אֶל-שִׁמְעוֹן׃
<A NAME="35"> </A>
<B class="number">35</B>&nbsp;וַיְסַפְּרוּ גַּם-הֵם אֵת אֲשֶׁר קָרָם בַּדָּרֶךְ וְאֵת אֲשֶׁר הִכִּירֻהוּ בִּבְצִיעַת הַלָּחֶם׃
</p>
