<p>
<A NAME="36"> </A>
<B class="number">36</B>&nbsp;עוֹדָם מְדַבְּרִים כַּדְּבָרִים הָאֵלֶּה וְיֵשׁוּעַ עַצְמוֹ עָמַד בְּתוֹכָם וַיֹּאמֶר אֲלֵיהֶם שָׁלוֹם לָכֶם׃
<A NAME="37"> </A>
<B class="number">37</B>&nbsp;וְהֵמָּה חַתּוּ וְנִבְעָתוּ וַיַּחְשְׁבוּ כִּי-רוּחַ הֵם רֹאִים׃
<A NAME="38"> </A>
<B class="number">38</B>&nbsp;וַיֹּאמֶר אֲלֵיהֶם מַה-לָּכֶם כִּי תִבָּהֵלוּ וְלָמָּה תַעֲלֶינָה מַחֲשָׁבוֹת בִּלְבַבְכֶם׃
<A NAME="39"> </A>
<B class="number">39</B>&nbsp;רְאוּ אֶת-יָדַי וְאֶת-רַגְלַי כִּי אָנֹכִי הוּא מַשְׁשׁוּנִי וּרְאוּ כִּי רוּחַ אֵין לוֹ בָּשָׂר וַעֲצָמוֹת כַּאֲשֶׁר אַתֶּם רֹאִים בִּי׃
<A NAME="40"> </A>
<B class="number">40</B>&nbsp;וַיְהִי כַּאֲשֶׁר דִּבֶּר אֶת-הַדְּבָרִים הָאֵלֶּה הֶרְאָה אֹתָם אֵת יָדָיו וְאֵת רַגְלָיו׃
<A NAME="41"> </A>
<B class="number">41</B>&nbsp;וְהֵם עוֹד לֹא הֶאֱמִינוּ מִשִּׂמְחָה וַיִּתְמָהוּ וַיֹּאמֶר אֲלֵיהֶם הֲיֵשׁ-לָכֶם פֹּה אֹכֶל׃
<A NAME="42"> </A>
