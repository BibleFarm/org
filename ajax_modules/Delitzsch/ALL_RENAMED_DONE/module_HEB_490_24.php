<p>
<A NAME="1"> </A>
<B class="number">1</B>&nbsp;וּבְאֶחָד בַּשַּׁבָּת לִפְנֵי עֲלוֹת הַשַּׁחַר בָּאוּ אֶל-הַקֶּבֶר וַתְּבִיאֶינָה אֶת-הַסַּמִּים אֲשֶׁר הֵכִינוּ וְעִמָּהֶן נָשִׁים אֲחֵרוֹת׃
