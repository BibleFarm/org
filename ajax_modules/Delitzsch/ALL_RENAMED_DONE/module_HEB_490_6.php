<h2>העץ ופריו</h2>
<h3>(<a href="../Matthew/Matthew07.htm#17">מתי ז' 20-17</a>;<a href="../Matthew/Matthew12.htm#ale">מתי יב' 35-34</a>)</h3>
<p>
<A NAME="43"> </A>
<B class="number">43</B>&nbsp;כִּי-עֵץ טוֹב אֵינֶנּוּ עֹשֶׂה פְּרִי נִשְׁחָת וְעֵץ נִשְׁחָת אֵינֶנּוּ עֹשֶׂה פְּרִי טוֹב׃
<A NAME="44"> </A>
<B class="number">44</B>&nbsp;כִּי כָל-עֵץ נִכַּר בְּפִרְיוֹ כִּי אֵין אֹסְפִים תְּאֵנִים מִן-הַקֹּצִים אַף אֵין-בֹּצְרִים עֵנָב מִן-הַסְּנֶה׃
<A NAME="5"> </A>
<B class="number">5</B>&nbsp;אִישׁ טוֹב מֵאוֹצַר לִבּוֹ הַטּוֹב מֵפִיק אֶת-הַטּוֹב וְאִישׁ רַע מֵאוֹצַר לִבּוֹ הָרַע מֵפִיק אֶת-הָרָע כִּי-מִשִּׁפְעַת לֵב אִישׁ יְמַלֵּל פִּיהוּ׃
</p>
