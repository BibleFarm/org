<h2>שפיטת הזולת</h2>
<h3>(<a href="../Matthew/Matthew07.htm#agb">מתי ז' 5-1</a>)</h3>
<p>
<A NAME="37"> </A>
<B class="number">37</B>&nbsp;וְאַל-תִּשְׁפְּטוּ וְלֹא תִשָּׁפֵטוּ אַל-תְּחַיְּבוּ וְלֹא תְחֻיָּבוּ נַקּוּ וְתִנָּקוּ׃
<A NAME="38"> </A>
<B class="number">38</B>&nbsp;תְּנוּ וְתִנָּתֵן לָכֶם וּמִדָּה יָפָה דְּחוּקָה וּגְדוּשָׁה וּמֻשְׁפָּעָה יָשִׁיבוּ אֶל-חֵיקְכֶם כִּי בַמִּדָּה אֲשֶׁר אַתֶּם מוֹדֲדִים יִמַּד לָכֶם׃
<A NAME="39"> </A>
<B class="number">39</B>&nbsp;וַיִּשָׂא מְשָׁלוֹ וַיֹּאמֶר אֲלֵיהֶם הֲיוּכַל עִוֵּר לְהַדְרִיךְ אֶת-הָעִוֵּר הֲלֹא יִפְּלוּ שְׁנֵיהֶם אֶל-הַשָּׁחַת׃
<A NAME="40"> </A>
<B class="number">40</B>&nbsp;אֵין תַּלְמִיד נַעֲלֶה עַל-רַבּוֹ וְדַיּוֹ לְכָל-תַּלְמִיד שָׁלֵם לִהְיוֹת כְּרַבּוֹ׃
<A NAME="41"> </A>
<B class="number">41</B>&nbsp;וְלָמָּה זֶּה אַתָּה רֹאֶה אֶת-הַקֵּסֶם אֲשֶׁר בְּעֵין אָחִיךָ וְאֶת-הַקּוֹרָה בְעֵינְךָ לֹא תַבִּיט׃
<A NAME="42"> </A>
<B class="number">42</B>&nbsp;וְאֵיךְ תֹּאמַר אֶל-אָחִיךָ אָחִי הַנִּיחָה לִּי וְאָסִיר אֶת-הַקֵּסֶם אֲשֶׁר בְּעֵינֶךָ וְאֵינְךָ רֹאֶה אֶת-הַקּוֹרָה אֲשֶׁר בְּעֵינֶךָ הֶחָנֵף הָסֵר בָּרִאשׁוֹנָה אֶת-הַקּוֹרָה מֵעֵינְךָ וְאַחֲרֵי-כֵן רָאֹה תִרְאֶה לְהָסִיר אֶת-הַקֵּסֶם אֲשֶׁר בְּעֵין אָחִיךָ׃
</p>
