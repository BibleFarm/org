<h2>ריפוי בעל היד היבשה</h2>
<h3>(<a href="../Matthew/Matthew12.htm#alc">מתי יב' 14-9</a>;<a href="../Mark/Mark03.htm#acb">מרקוס ג' 6-1</a>)</h3>
<p>
<A NAME="6"> </A>
<B class="number">6</B>&nbsp;וַיְהִי בְּשַׁבָּת אַחֶרֶת וַיָּבֹא אֶל-בֵּית הַכְּנֵסֶת וַיְלַמֵּד וְשָׁם אִישׁ אֲשֶׁר יָבְשָׁה יָדוֹ הַיְמָנִית׃
<A NAME="7"> </A>
<B class="number">7</B>&nbsp;וַיֶּאֶרְבוּ-לוֹ הַסּוֹפְרִים וְהַפְּרוּשִׁים לִרְאוֹת אִם-יִרְפָּא בַּשַּׁבָּת לְמַעַן יִמְצְאוּ עָלָיו עֲלִילֹת דְּבָרִים׃
<A NAME="8"> </A>
<B class="number">8</B>&nbsp;וְהוּא יָדַע אֶת-מַחְשְׁבוֹתָם וַיֹּאמֶר אֶל-הָאִישׁ אֲשֶׁר יָבְשָׁה יָדוֹ קוּם וַעֲמוֹד בַּתָּוֶךְ וַיָּקָם וַיַּעֲמֹד׃
<A NAME="9"> </A>
<B class="number">9</B>&nbsp;וַיֹּאמֶר אֲלֵיהֶם יֵשׁוּעַ אֶשְׁאֲלָה אֶתְכֶם דָּבָר מַה-הַנָּכוֹן בַּשַּׁבָּת הַלְהֵיטִיב אִם לְהָרֵעַ לְהַצִּיל נֶפֶשׁ אִם-לְאַבֵּד׃
<A NAME="10"> </A>
<B class="number">10</B>&nbsp;וַיַּבֵּט סָבִיב אֶל-כֻּלָּם וַיֹּאמֶר לָאִישׁ פְּשֹׁט אֶת-יָדֶךָ וַיַּעַשׂ כֵּן וַתֵּרָפֵא יָדוֹ וַתָּשָׁב כָּאַחֶרֶת׃(<a href="../../Tenach/MelachimA/MelachimA13.htm#6">מלכים א', יג' ו'</a>)
<A NAME="11"> </A>
<B class="number">11</B>&nbsp;וְהֵמָּה נִמְלְאוּ חֵמָה וַיִּוָּסְדוּ יַחַד מַה-לַּעֲשׂוֹת לְיֵשׁוּעַ׃
</p>
