<p>
<A NAME="46"> </A>
<B class="number">46</B>&nbsp;וְלָמָּה זֶּה אַתֶּם קֹרְאִים לִי אֲדֹנִי אֲדֹנִי וְאֵינְכֶם עֹשִׂים אֵת אֲשֶׁר-אֲנִי אֹמֵר׃
