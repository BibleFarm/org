<h2>אהבה לאויבים</h2>
<h3>(<a href="../Matthew/Matthew05.htm#aej">מתי ה' 48-38</a>;<a href="../Matthew/Matthew07.htm#agca">ז' 12</a>)</h3>
<p>
<A NAME="27"> </A>
<B class="number">27</B>&nbsp;אֲבָל אַתֶּם הַשֹּׁמְעִים אֲלֵיכֶם אֲנִי אֹמֵר אֶהֱבוּ אֶת-אֹיְבֵיכֶם הֵיטִיבוּ לְשׂנְאֵיכֶם׃(<a href="../../Tenach/Shemot/Shemot23.htm#4">שמות כג' ד'</a>;&nbsp;<a href="../../Tenach/Mishley/Mishley25.htm#21">משלי כה' כא'</a>)
<A NAME="28"> </A>
<B class="number">28</B>&nbsp;בָּרֲכוּ אֶת-מְקַלֲלֵיכֶם וְהִתְפַּלֲלוּ בְּעַד מַכְלִימֵיכֶם׃
<a name="afga"> </a>
<A NAME="29"> </A>
<B class="number">29</B>&nbsp;הַמַּכֶּה אֹתְךָ עַל-הַלְּחִי הַטֵּה-לוֹ גַּם אֶת-הָאַחֶרֶת וְהַלּקֵחַ אֶת-מְעִילְךָ אַל-תִּמְנַע מִמֶּנּוּ גַּם אֶת-כֻּתָּנְתֶּךָ׃
<A NAME="30"> </A>
<B class="number">30</B>&nbsp;וְכָל-הַשֹּׁאֵל מִמְּךָ תֶּן-לוֹ וְהַלּקֵחַ אֵת אֲשֶׁר לָךְ אַל-תִּתְבַּע מֵאִתּוֹ׃(<a href="../../Tenach/Devarim/Devarim15.htm#7">דברים טו' ז'-ח'</a>)
<A NAME="31"> </A>
<B class="number">31</B>&nbsp;וְכַאֲשֶׁר תִּרְצוּ שֶׁיַּעֲשׂוּ לָכֶם בְּנֵי הָאָדָם כֵּן תַּעֲשׂוּ-לָהֶם גַּם-אַתֶּם׃
<A NAME="32"> </A>
<B class="number">32</B>&nbsp;וְאִם-תְּאֵהֲבוּ אֶת-אֹהֲבֵיכֶם מֶה חַסְדְּכֶם כִּי גַם-הַחַטָּאִים אֹהֲבִים אֶת-אֹהֲבֵיהֶם׃
<A NAME="33"> </A>
<B class="number">33</B>&nbsp;וְאִם תֵּיטִיבוּ לְמֵטִיבֵיכֶם מֶה חַסְדְּכֶם גַּם-הַחַטָּאִים יַעֲשׂוּ-כֵן׃
<A NAME="34"> </A>
<B class="number">34</B>&nbsp;וְאִם-תַּלְווּ אֶת-הָאֲנָשִׁים אֲשֶׁר תְּקַוּוּ לְקַבֵּל מֵהֶם מֶה חַסְדְּכֶם גַּם הַחַטָּאִים מַלְוִים אֶת-הַחַטָּאִים לְמַעַן יוּשַׁב לָהֶם הַמִּלְוֶה׃
<A NAME="35"> </A>
<B class="number">35</B>&nbsp;אֲבָל אֶהֱבוּ אֶת-אֹיְבֵיכֶם וְהֵיטִיבוּ וְהַלְווּ וְאַל תְּצַפּוּ לְתַשְׁלוּם וִיהִי שְׂכַרְכֶם רַב וִהְיִיתֶם בְּנֵי עֶלְיוֹן כִּי טוֹב הוּא גַּם-לִכְפוּיֵי טוֹבָה וְלָרָעִים׃
<A NAME="36"> </A>
<B class="number">36</B>&nbsp;לָכֵן הֱיוּ רַחֲמָנִים כַּאֲשֶׁר גַּם-אֲבִיכֶם רַחוּם הוּא׃
</p>
