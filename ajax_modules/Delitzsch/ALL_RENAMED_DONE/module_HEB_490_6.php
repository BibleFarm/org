<h2>אושר לעומת אבל</h2>
<h3>(<a href="../Matthew/Matthew05.htm#aec">מתי ה' 12-3</a>)</h3>
<p>
<A NAME="20"> </A>
<B class="number">20</B>&nbsp;וְהוּא נָשָׂא אֶת-עֵינָיו אֶל-תַּלְמִידָיו וַיֹּאמַר אַשְׁרֵיכֶם אַתֶּם הָעֲנִיִּים כִּי-לָכֶם מַלְכוּת הָאֱלֹהִים׃
<A NAME="21"> </A>
<B class="number">21</B>&nbsp;אַשְׁרֵיכֶם אַתֶּם הָרְעֵבִים הַיּוֹם כִּי תִשְׂבָּעוּ אַשְׁרֵיכֶם הַבֹּכִים הַיּוֹם כִּי תִשְׂחָקוּ׃(<a href="../../Tenach/Tehillim/Tehillimc6.htm#5">תהילים קכו' ה'-ו'</a>)
<A NAME="22"> </A>
<B class="number">22</B>&nbsp;אַשְׁרֵיכֶם אִם-יִשְׂנְאוּ אֶתְכֶם הָאֲנָשִׁים וְאִם-יְנַדּוּ אֶתְכֶם וְחֵרְפוּ וִינָאֲצוּ אֶת-שִׁמְכֶם כְּשֵׁם רָע לְמַעַן בֶּן-הָאָדָם׃(<a href="../../Tenach/Yishayah/Yishayah66.htm#5">ישעיהו סו' ה'</a>)
<A NAME="23"> </A>
<B class="number">23</B>&nbsp;שִׂמְחוּ בַּיּוֹם הַהוּא וּרְקֹדוּ כִּי הִנֵּה שְׂכַרְכֶם רַב בַּשָּׁמַיִם כִּי-כַדָּבָר הַזֶּה עָשׂוּ אֲבֹתֵיהֶם לַנְּבִיאִים׃
<A NAME="24"> </A>
<B class="number">24</B>&nbsp;אַךְ-אוֹי לָכֶם הָעֲשִׁירִים כִּי-כְבָר לְקַחְתֶּם אֵת נֶחָמַתְכֶם׃
<A NAME="25"> </A>
<B class="number">25</B>&nbsp;אוֹי לָכֶם הַשְׂבֵעִים כִּי תִרְעָבוּ אוֹי לָכֶם הַשּׂחֲקִים הַיּוֹם כִּי תִתְאַבְּלוּ וְתִבְכּוּ׃
<A NAME="26"> </A>
<B class="number">26</B>&nbsp;אוֹי לָכֶם אִם כָּל-הָאֲנָשִׁים מְשַׁבְּחִים אֶתְכֶם כִּי כַּדָּבָר הַזֶּה עָשׂוּ אֲבוֹתֵיהֶם לִנְבִיאֵי הַשָּׁקֶר׃(<a href="../../Tenach/Yirmiah/Yirmiah05.htm#31">ירמיהו ה' לא'</a>)
</p>
