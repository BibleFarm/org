<h2>בחירת שנים-עשר השליחים</h2>
<h3>(<a href="../Matthew/Matthew10.htm#ajb">מתי י' 4-1</a>;<a href="../Mark/Mark03.htm#acd">מרקוס ג' 19-13</a>)</h3>
<p>
<A NAME="12"> </A>
<B class="number">12</B>&nbsp;וַיְהִי בַּיָּמִים הָהֵם וַיֵּצֵא הָהָרָה לְהִתְפַּלֵּל וַיַּעֲמֹד כָּל-הַלַּיְלָה בַּתְּפִלָּה לֵאלֹהִים׃(<a href="../../Tenach/Tehillim/Tehillima9.htm#4">תהילים קט' ד'</a>)
<A NAME="13"> </A>
<B class="number">13</B>&nbsp;וּבִהְיֹת הַבֹּקֶר אָסַף אֵלָיו אֶת-תַּלְמִידָיו וַיִּבְחַר מֵהֶם שְׁנֵים עָשָׂר אֲשֶׁר קָרָא לָהֶם שְׁלִיחִים׃
<A NAME="14"> </A>
<B class="number">14</B>&nbsp;אֶת-שִׁמְעוֹן אֲשֶׁר גַּם-קְרָאוֹ פֶּטְרוֹס וְאֶת-אַנְדְּרַי אָחִיו אֶת-יַעֲקֹב וְאֵת יוֹחָנָן אֵת פִּילִפּוֹס וְאֵת בַּר-תַּלְמָי׃
<A NAME="15"> </A>
<B class="number">15</B>&nbsp;אֵת מַתַּי וְאֶת-תּוֹמָא וְאֶת-יַעֲקֹב בֶּן-חַלְפַי וְאֶת-שִׁמְעוֹן אֲשֶׁר יִקָּרֵא לוֹ הַקַּנָּא׃
<A NAME="16"> </A>
<B class="number">16</B>&nbsp;אֶת-יְהוּדָה בֶּן-יַעֲקֹב וְאֶת-יְהוּדָה אִישׁ-קְרִיּוֹת וְהוּא אֲשֶׁר הָיָה לְמוֹסֵר׃
</p>
