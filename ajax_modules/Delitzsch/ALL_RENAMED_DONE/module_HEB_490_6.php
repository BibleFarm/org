<h2>ישוע משרת את ההמונים</h2>
<h3>(<a href="../Matthew/Matthew04.htm#ade">מתי ד' 25-23</a>)</h3>
<p>
<A NAME="17"> </A>
<B class="number">17</B>&nbsp;וַיֵּרֶד אִתָּם וַיַּעֲמֹד בִּמְקוֹם מִישׁוֹר הוּא וַהֲמוֹן תַּלְמִידָיו וּקְהַל עַם רָב מִכָּל-יְהוּדָה וִירוּשָׁלַיִם וּמֵחוֹף יָם-צֹר וְצִידוֹן אֲשֶׁר בָּאוּ לִשְׁמֹעַ אֹתוֹ וּלְהֵרָפֵא מֵחָלְיֵיהֶם׃
<A NAME="18"> </A>
<B class="number">18</B>&nbsp;וְגַם-הַמְעֻנִּים בְּרוּחוֹת טְמֵאוֹת וַיֵּרָפֵאוּ׃
<A NAME="19"> </A>
<B class="number">19</B>&nbsp;וְכָל-הֶהָמוֹן מְבַקְשִׁים לָגַעַת בּוֹ כִּי גְבוּרָה יָצְאָה מֵאִתּוֹ וְרָפְאָה אֶת-כֻּלָּם׃
</p>
