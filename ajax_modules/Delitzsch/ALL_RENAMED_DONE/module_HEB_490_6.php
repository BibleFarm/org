<h2>בן-האדם הוא אדון השבת</h2>
<h3>(<a href="../Matthew/Matthew12.htm#alb">מתי יב' 8-1</a>;<a href="../Mark/Mark02.htm#abe">מרקוס ב' 28-23</a>)</h3>
<p>
<A NAME="1"> </A>
<B class="number">1</B>&nbsp;וַיְהִי בָּיוֹם הַשַּׁבָּת הַשֵּׁנִית לִסְפִירַת הָעֹמֶר עָבַר בֵּין הַשָׂדוֹת וַיִּקְטְפוּ תַלְמִידָיו מְלִילֹת וַיְפָרְכוּ אֹתָן בִּידֵיהֶם וַיֹּאכֵלוּ׃
<A NAME="2"> </A>
<B class="number">2</B>&nbsp;וַאֲנָשִׁים מִן-הַפְּרוּשִׁים אָמְרוּ אֲלֵיהֶם לָמָה אַתֶּם עֹשִׂים אֵת אֲשֶׁר לֹא יֵעָשֶׂה בַּשַּׁבָּת׃
<A NAME="3"> </A>
<B class="number">3</B>&nbsp;וַיַּעַן יֵשׁוּעַ וַיֹּאמֶר אֲלֵיהֶם הֲלֹא קְרָאתֶם אֵת אֲשֶׁר עָשָׂה דָוִד בִּהְיוֹתוֹ רָעֵב הוּא וַאֲשֶׁר הָיוּ אִתּוֹ׃
<A NAME="4"> </A>
<B class="number">4</B>&nbsp;אֲשֶׁר בָּא אֶל-בֵּית הָאֱלֹהִים וַיִּקַּח אֶת-לֶחֶם הַפָּנִים וַיֹּאכַל וְגַם-נָתַן לַאֲנָשָׁיו אֵת אֲשֶׁר לֹא נָכוֹן לְאָכְלוֹ כִּי אִם-לַכֹּהֲנִים לְבַדָּם׃(<a href="../../Tenach/ShemuelA/ShemuelA21.htm#2">שמואל א', כא' ב'-ז'</a>)
<A NAME="5"> </A>
<B class="number">5</B>&nbsp;וַיֹּאמֶר אֲלֵיהֶם כִּי בֶּן-הָאָדָם גַּם-אֲדוֹן הַשַּׁבָּת הוּא׃
</p>
