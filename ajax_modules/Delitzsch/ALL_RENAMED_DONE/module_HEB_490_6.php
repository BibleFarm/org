<A NAME="48"> </A>
<B class="number">48</B>&nbsp;דּוֹמֶה הוּא לְאִישׁ בֹּנֶה-בַּיִת אֲשֶׁר הֶעְמִיק לַחְפֹּר וַיְיַסְּדוֹ עַל-הַצּוּר וּכְבוֹא הַשֵּׁטֶף פָּרַץ הַנַּחַל בַּבַּיִת הַהוּא וְלֹא יָכֹל לַהֲנִיעוֹ [<span class="x" onmousemove="Show('perush','נוסח אחר&#58; כִּי עַל-הַסֶּלַע יְסוֹדוֹ' ,event);" onmouseout="Hide();">כִּי-טוֹב מִבְנֵהוּ</span>]׃(<a href="../../Tenach/Mishley/Mishley10.htm#25">משלי י' כה'</a>)
