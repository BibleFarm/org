<h1>אגרת יוחנן השלישית<br>
פרק א</h1>
<p>
<A NAME="1"> </A>
<B class="number">1</B>&nbsp;הַזָּקֵן אֶל-גָּיוֹס יְדִידִי אֲשֶׁר אֲנִי אֹהֵב אֹתוֹ בֶּאֱמֶת׃ 
<A NAME="2"> </A>
<B class="number">2</B>&nbsp;יְדִידִי רְצוֹנִי שֶׁיִּיטַב לְךָ לְכָל-דָּבָר וְתִהְיֶה בָרִיא כְּטוֹב לְנַפְשֶׁךָ׃ 
<A NAME="3"> </A>
<B class="number">3</B>&nbsp;כִּי שָׂמַחְתִּי מְאֹד כַּאֲשֶׁר בָּאוּ אַחִים וַיָּעִידוּ עַל-אֲמִתֶּךָ אֲשֶׁר מִתְהַלֵּךְ אַתָּה בָּאֱמֶת׃ 
<A NAME="4"> </A>
<B class="number">4</B>&nbsp;אֵין לִי שִׂמְחָה גְדוֹלָה מִלִּשְׁמֹעַ אֶת-בָּנַי מִתְהַלְּכִים בָּאֱמֶת׃ 
<A NAME="5"> </A>
<B class="number">5</B>&nbsp;יְדִידִי בֶּאֱמוּנָה כָּל-מַעֲשֶׂיךָ עִם-הָאַחִים וְעִם-הָאֹרְחִים׃ 
<A NAME="6"> </A>
<B class="number">6</B>&nbsp;אֲשֶׁר הֵעִידוּ עַל-אַהֲבָתְךָ בִּפְנֵי הַקָּהָל וְיָפֶה תַעֲשֶׂה לְשַׁלַּח אֹתָם כָּרָאוּי לִפְנֵי אֱלֹהִים׃ 
<A NAME="7"> </A>
<B class="number">7</B>&nbsp;כִּי לְמַעַן שְׁמוֹ יָצָאוּ וְלֹא לָקְחוּ דָבָר מִן-הַגּוֹיִם׃ 
<A NAME="8"> </A>
<B class="number">8</B>&nbsp;עַל-כֵּן עָלֵינוּ לְקַבֵּל אֹתָם לְמַעַן נִהְיֶה עֹזְרִים לָאֱמֶת׃ 
<A NAME="9"> </A>
<B class="number">9</B>&nbsp;אֲנִי כָתַבְתִּי אֶל-הַקְּהִלָּה אֲבָל דִּיּוֹטְרִיפַס הַמִּתְאַוֶּה לִהְיוֹת לָהֶם לְרֹאשׁ אֵינֶנּוּ מְקַבֵּל אֹתָנוּ׃ 
<A NAME="10"> </A>
<B class="number">10</B>&nbsp;עַל-כֵּן בְּבֹאִי אַזְכִּיר אֶת-מַעֲשָׂיו אֲשֶׁר הוּא עֹשֶׂה לְבַטֵּא עָלֵינוּ דְּבָרִים רָעִים וְלֹא דַּי-לוֹ בָזֶה כִּי גַם אֶת-הָאַחִים לֹא יְקַבֵּל וְיִמְנַע אֶת-הַחֲפֵצִים לְקַבְּלָם וַיְגָרֲשֵׁם מִתּוֹךְ הַקָּהָל׃ 
<A NAME="11"> </A>
<B class="number">11</B>&nbsp;יְדִידִי אַל-תֵּלֵךְ בְּעִקְּבוֹת הָרָעָה כִּי אִם-בְּעִקְּבוֹת הַטּוֹב הָעֹשֶׂה טוֹב הוּא מֵאֱלֹהִים וְהָעֹשֶׂה רַע לֹא רָאָה אֶת-הָאֱלֹהִים׃(<a href="../../Tenach/Tehillim/Tehillim34.htm#15">תהילים לד' טו'</a>) 
<A NAME="12"> </A>
<B class="number">12</B>&nbsp;עַל-דְּמַטְרִיּוֹס הֵעִידוּ הַכֹּל וְגַם-הָאֱמֶת עַצְמָהּ וְגַם-אֲנַחְנוּ מְעִידִים עָלָיו וִידַעְתֶּם כִּי עֵדוּתֵנוּ נֶאֱמָנָה׃ 
<A NAME="13"> </A>
<B class="number">13</B>&nbsp;הַרְבֵּה יֶשׁ-לִי לִכְתֹּב וְלֹא חָפַצְתִּי לִכְתֹּב אֵלֶיךָ בִּדְיוֹ וּבְקוֹלָמוֹס׃ 
<A NAME="14"> </A>
<B class="number">14</B>&nbsp;אֲבָל אֲקַוֶּה לִרְאוֹתְךָ בִּמְהֵרָה וּפֶה אֶל-פֶּה נְדַבֵּר׃ 
<A NAME="15"> </A>
<B class="number">15</B>&nbsp;שָׁלוֹם לָךְ הָרֵעִים שֹׁאֲלִים לִשְׁלוֹמֶךָ שְׁאַל לִשְׁלוֹם הָרֵעִים לְאִישׁ אִישׁ בִּשְׁמוֹ׃(<a href="../../Tenach/Bemidbar/Bemidbar06.htm#26">במדבר ו' כו'</a>) 
</p>
