<h1>התגלות<br>
פרק יא</h1>
<a name="akb"> </a>
<h2>שני העדים</h2>
<p>
<A NAME="1"> </A>
<B class="number">1</B>&nbsp;וַיִּנָּתֶן-לִי קָנֶה וְהוּא כְמַטֶּה וַיַּעֲמֹד הַמַּלְאָךְ וַיֹּאמֶר קוּם וּמֹד אֶת-הֵיכַל יְהוָֹה וְאֵת הַמִּזְבֵּחַ וְאֵת הַמִּשְׁתַּחֲוִים בּוֹ׃(<a href="../../Tenach/Yechezkel/Yechezkel42.htm#15">יחזקאל מב' טו'</a>)
<A NAME="2"> </A>
<B class="number">2</B>&nbsp;וְהֶחָצֵר אֲשֶׁר [<span class="x" onmousemove="Show('perush','נוסח אחר&#58; לִפְנִימָה' ,event);" onmouseout="Hide();">מִחוּץ</span>] לַהֵיכָל אֹתָהּ נְטֹשׁ וְאַל-תְּמֻדֶּנָּה כִּי-לַגּוֹיִם נִתָּנָה וְרָמְסוּ אֶת-עִיר הַקֹּדֶשׁ אַרְבָּעִים וּשְׁנַיִם חֳדָשִׁים׃(<a href="../../Tenach/Tehillim/Tehillim79.htm#1">תהילים עט' א'</a>;&nbsp;<a href="../../Tenach/Yishayah/Yishayah63.htm#18">ישעיהו סג' יח'</a>;&nbsp;<a href="../../Tenach/Daniel/Daniel12.htm#7">דניאל יב' ז'</a>)
<A NAME="3"> </A>
<B class="number">3</B>&nbsp;וְנָתַתִּי לִשְׁנֵי עֵדַי וְנִבְּאוּ יָמִים אֶלֶף וּמָאתַיִם וְשִׁשִּׁים וְהֵם מִתְכַּסִּים בַּשַׂקִּים׃(<a href="../../Tenach/Devarim/Devarim19.htm#15">דברים יט' טו'</a>)
<A NAME="4"> </A>
<B class="number">4</B>&nbsp;אֵלֶּה הֵם שְׁנֵי הַזֵּיתִים וּשְׁתֵּי הַמְּנֹרוֹת הָעֹמְדִים לִפְנֵי אֱלֹהֵי הָאָרֶץ׃(<a href="../../Tenach/Zecharyah/Zecharyah04.htm#3">זכריה ד' ג'</a>;&nbsp;<a href="../../Tenach/Zecharyah/Zecharyah04.htm#11">יא'-יד'</a>)
<A NAME="5"> </A>
<B class="number">5</B>&nbsp;וְכִי-יְבַקֵּשׁ אִישׁ לְהָרַע לָהֶם אֵשׁ יֹצֵאת מִפִּיהֶם (<a href="../../Tenach/ShemuelB/ShemuelB22.htm#9">שמואל ב', כב' ט'</a>) אֹכֶלֶת אֶת-אֹיְבֵיהֶם וְכֵן כָּל-הַמְבַקֵּשׁ לְהָרַע לָהֶם מוֹת יוּמָת׃
<A NAME="6"> </A>
<B class="number">6</B>&nbsp;וְהֵם יֶשׁ-לְאֵל יָדָם לַעֲצֹר אֶת-הַשָּׁמַיִם וְלֹא-יִהְיֶה מָטָר בִּימֵי נְבוּאָתָם (<a href="../../Tenach/MelachimA/MelachimA17.htm#1">מלכים א', יז' א'</a>) וְיִשְׁלְטוּ עַל-הַמַּיִם לְהָפְכָם לְדָם וְעַל-הָאָרֶץ לְהַכֹּתָהּ בְּכָל-נֶגַע מִדֵּי יֶחְפָּצוּ׃(<a href="../../Tenach/Shemot/Shemot07.htm#20">שמות ז' כ'-כא'</a>)
<A NAME="7"> </A>
<B class="number">7</B>&nbsp;וּבְכַלּוֹתָם עֵדוּתָם הַחַיָּה הָעֹלָה מִתְּהוֹם תַּעֲשֶׂה עִמָּהֶם מִלְחָמָה וְתוּכַל לָהֶם וַהֲרָגָתַם׃(<a href="../../Tenach/Daniel/Daniel07.htm#7">דניאל ז' ז'</a>;&nbsp;<a href="../../Tenach/Daniel/Daniel07.htm#21">ז' כא'</a>)
<A NAME="8"> </A>
<B class="number">8</B>&nbsp;וְהָיְתָה נִבְלָתָם בִּרְחוֹב הָעִיר הַגְּדוֹלָה הַנִּקְרֵאת כְּפִי הָרוּחַ בְּשֵׁם סְדוֹם וּמִצְרַיִם אֲשֶׁר-שָׁם נִצְלַב גַּם-[<span class="x" onmousemove="Show('perush','נוסח אחר&#58; אֲדֹנֵינוּ' ,event);" onmouseout="Hide();">אֲדֹנֵיהֶם</span>]׃
<A NAME="9"> </A>
<B class="number">9</B>&nbsp;וְרַבִּים מִן-הָעַמִּים וְהַמִּשְׁפָּחוֹת וְהַלְּשֹׁנוֹת וְהַגּוֹיִם יִרְאוּ אֶת-נִבְלָתָם יָמִים שְׁלֹשָׁה וָחֵצִי וְלֹא יִתְּנוּ לְשׂוּם אֶת-גְּוִיֹּתָם בַּקְּבָרִים׃(<a href="../../Tenach/Tehillim/Tehillim79.htm#3">תהילים עט' ג'</a>)
<A NAME="10"> </A>
<B class="number">10</B>&nbsp;וְיֹשְׁבֵי הָאָרֶץ יִשְׂמְחוּ עֲלֵיהֶם וְיַעַלְזוּ וְיִשְׁלְחוּ מָנוֹת אִישׁ לְרֵעֵהוּ כִּי שְׁנֵי הַנְּבִיאִים הָאֵלֶּה הִכְאִיבוּ אֶת-יֹשְׁבֵי הָאָרֶץ׃
<A NAME="11"> </A>
<B class="number">11</B>&nbsp;וַיְהִי אַחֲרֵי יָמִים שְׁלֹשָׁה וָחֵצִי וַתָּבֹא בָהֶם רוּחַ חַיִּים מֵאֵת הָאֱלֹהִים וַיַּעַמְדוּ עַל-רַגְלֵיהֶם וְאֵימָה גְדוֹלָה נָפְלָה עַל-כָּל-רֹאֵיהֶם׃(<a href="../../Tenach/Yechezkel/Yechezkel37.htm#10">יחזקאל לז' י'</a>)
<A NAME="12"> </A>
<B class="number">12</B>&nbsp;וַיִּשְׁמְעוּ קוֹל גָּדוֹל מִן-הַשָּׁמַיְמָה מִדַּבֵּר אֲלֵיהֶם לֵאמֹר עֲלוּ הֵנָּה וַיַּעֲלוּ בֶעָנָן הַשָּׁמַיְמָה וְעֵינֵי שׂנְאֵיהֶם רֹאוֹת׃
<A NAME="13"> </A>
<B class="number">13</B>&nbsp;וּבַשָּׁעָה הַהִיא הָיָה רַעַשׁ גָּדוֹל וַתִּפֹּל עֲשִׂירִית הָעִיר וְשִׁבְעַת אֲלָפִים בְּנֵי-אָדָם לְמִסְפַּר שְׁמוֹתָם נֶהֶרְגוּ בָרָעַשׁ וְהַנִּשְׁאָרִים רְעָדָה אֲחָזָתַם וַיִּתְּנוּ כָבוֹד לֵאלֹהֵי הַשָּׁמָיִם׃
<A NAME="14"> </A>
<B class="number">14</B>&nbsp;אוֹי שֵׁנִי חָלַף הָלַךְ לוֹ וְאוֹי שְׁלִישִׁי מְהֵרָה יָבוֹא׃
</p>
<a name="akc"> </a>
<h2>השופר השביעי</h2>
<p>
<A NAME="15"> </A>
<B class="number">15</B>&nbsp;וְהַמַּלְאָךְ הַשְּׁבִיעִי תָּקַע בַּשּׁוֹפָר וַיְהִי קֹלוֹת גְּדוֹלִים בַּשָּׁמַיִם וַיֹּאמְרוּ הִנֵּה מַמְלֶכֶת הָעוֹלָם הָיְתָה לַאֲדֹנֵינוּ וְלִמְשִׁיחוֹ וְהוּא יִמְלֹךְ לְעוֹלְמֵי עוֹלָמִים׃(<a href="../../Tenach/Daniel/Daniel02.htm.org#44">דניאל ב' מד'</a>)
<A NAME="16"> </A>
<B class="number">16</B>&nbsp;וְעֶשְׂרִים וְאַרְבָּעָה הַזְּקֵנִים הַיּשְׁבִים לִפְנֵי הָאֱלֹהִים עַל-כִּסְאוֹתָם נָפְלוּ עַל-פְּנֵיהֶם וַיִּשְׁתַּחֲווּ לֵאלֹהִים׃
<A NAME="17"> </A>
<B class="number">17</B>&nbsp;וַיֹּאמְרוּ מוֹדִים אֲנַחְנוּ לְךָ יְהוָֹה אֱלֹהִים צְבָאוֹת הַהוֶֹה וְהָיָה וְיָבוֹא כִּי לָבַשְׁתָּ עֻזְּךָ הַגָּדוֹל וַתִּמְלֹךְ׃(<a href="../../Tenach/Tehillim/Tehillime5.htm#13">תהילים קמה' יג' </a>)
<A NAME="18"> </A>
<B class="number">18</B>&nbsp;וְהַגּוֹיִם קָצָפוּ וַיָּבֹא קֶצְפְּךָ (<a href="../../Tenach/Tehillim/Tehillim02.htm#1">תהילים ב' א'-ב'</a>;&nbsp;<a href="../../Tenach/Tehillim/Tehillim02.htm#5">ב' ה'</a>) וְעֵת לִשְׁפֹּט הַמֵּתִים וְלָתֵת שָׂכָר לַעֲבָדֶיךָ הַנְּבִיאִים וְלַקְדֹשִׁים וּלְיִרְאֵי שְׁמֶךָ לְמִקְּטַנָּם וְעַד-גְּדוֹלָם וּלְהַשְׁחִית אֶת-מַשְׁחִיתֵי הָאָרֶץ׃(<a href="../../Tenach/Yishayah/Yishayah40.htm#10">ישעיהו מ' י'</a>;&nbsp;<a href="../../Tenach/Yishayah/Yishayah61.htm#8">סא' ח'</a>)
<A NAME="19"> </A>
<B class="number">19</B>&nbsp;וַיִּפָּתַח הֵיכַל יְהוָֹה בַּשָּׁמַיִם וַיֵּרָא אֲרוֹן בְּרִיתוֹ בְּהֵיכָלוֹ (<a href="../../Tenach/Shemot/Shemot25.htm#9">שמות כה' ט'</a>) וַיְהִי בְּרָקִים וְקֹלוֹת וּרְעָמִים וְרַעַשׁ וּבָרָד כָּבֵד׃(<a href="../../Tenach/Shemot/Shemot19.htm#16">שמות יט' טז'</a>)
</p>
