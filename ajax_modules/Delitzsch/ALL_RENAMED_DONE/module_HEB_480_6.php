<h2>הליכה על פני המים</h2>
<h3>(<a href="../Matthew/Matthew14.htm#and">מתי יד' 33-22</a>;<a href="../John/John06.htm#afc">יוחנן ו' 21-15</a>)</h3>
<p>
<A NAME="45"> </A>
<B class="number">45</B>&nbsp;וְאַחֲרֵי-כֵן הֵאִיץ בְּתַלְמִידָיו לָרֶדֶת בָּאֳנִיָּה וְלַעֲבוֹר לְפָנָיו אֶל-עֵבֶר הַיָּם אֶל-בֵּית צָיְדָה עַד-שַׁלְּחוֹ אֶת-הָעָם׃
<A NAME="46"> </A>
<B class="number">46</B>&nbsp;וַיְהִי אַחַר שַׁלְּחוֹ אֹתָם וַיַּעַל הָהָרָה לְהִתְפַּלֵּל׃
<A NAME="47"> </A>
<B class="number">47</B>&nbsp;וַיְהִי-עֶרֶב וְהָאֳנִיָּה בָאָה בְּתוֹךְ הַיָּם וְהוּא לְבַדּוֹ בַּיַּבָּשָׁה׃
<A NAME="48"> </A>
<B class="number">48</B>&nbsp;וַיַּרְא אוֹתָם מִתְיַגְּעִים בְּשׁוּטָם כִּי הָרוּחַ לְנֶגְדָּם וַיְהִי כְּעֵת הָאַשְׁמֹרֶת הָרְבִיעִית וַיָּבֹא אֲלֵיהֶם מִתְהַלֵּךְ עַל-פְּנֵי הַיָּם (<a href="../../Tenach/Iyov/Iyov09.htm#8">איוב ט' ח'</a>) וַיּוֹאֶל לַעֲבוֹר לִפְנֵיהֶם׃
<A NAME="49"> </A>
<B class="number">49</B>&nbsp;וַיִּרְאוּ אֹתוֹ מִתְהַלֵּךְ עַל-פְּנֵי הַיָּם וַיֹּאמְרוּ מַרְאֵה-רוּחַ הוּא וַיִּצְעָקוּ׃
<A NAME="50"> </A>
<B class="number">50</B>&nbsp;כִּי-כֻלָּם רָאוּהוּ וַיִּבָּהֵלוּ אָז דִבֶּר אִתָּם וַיֹּאמֶר אֲלֵיהֶם חִזְקוּ כִּי-אֲנִי הוּא אַל-תִּירָאוּ׃
<A NAME="51"> </A>
<B class="number">51</B>&nbsp;וַיֵּרֶד אֲלֵיהֶם בָּאֳנִיָּה וְהָרוּחַ שָׁכָכָה וַיִּשְּׁתּוֹמֵם לִבָּם עוֹד-יוֹתֵר וַיִּתְמָהוּ׃
<A NAME="52"> </A>
<B class="number">52</B>&nbsp;כִּי לֹא הִשְׂכִּילוּ בִּדְבַר-כִּכְּרוֹת הַלָּחֶם מִפְּנֵי קְשִׁי לְבָבָם׃
</p>
