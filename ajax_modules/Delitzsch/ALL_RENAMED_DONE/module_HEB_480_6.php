<h2>ריפוי חולים בגינוסר</h2>
<h3>(<a href="../Matthew/Matthew14.htm#ane">מתי יד' 36-34</a>)</h3>
<p>
<A NAME="53"> </A>
<B class="number">53</B>&nbsp;וַיַּעַבְרוּ אֶת-הַיָּם וַיָּבֹאוּ אַרְצָה גִּנֵּיסַר וַיִּקְרְבוּ אֶל-הַיַּבָּשָׁה׃
<A NAME="54"> </A>
<B class="number">54</B>&nbsp;וַיְהִי כְּצֵאתָם מִן-הָאֳנִיָּה וַיַּכִּירֻהוּ׃
<A NAME="55"> </A>
<B class="number">55</B>&nbsp;וַיָּרוּצוּ בְּכָל-הַכִּכָּר הַהוּא מִסָּבִיב וַיָּחֵלּוּ לָשֵׂאת אֶת-הַחֹלִים בְּמִשְׁכָּבוֹת אֶל-כָּל-מָקוֹם אֲשֶׁר שָׁמְעוּ כִּי הוּא שָׁם׃
<A NAME="56"> </A>
<B class="number">56</B>&nbsp;וּבְכָל-מָקוֹם אֲשֶׁר יָבֹא אֶל-הַכְּפָרִים אוֹ אֶל-הֶעָרִים וְאֶל-הַשָׂדוֹת שָׁם שָׂמוּ אֶת-הַחוֹלִים בַּחוּצוֹת וַיִּתְחַנְּנוּ לוֹ שֶׁיִּגְּעוּ רַק בִּכְנַף בִּגְדוֹ (<a href="../../Tenach/Bemidbar/Bemidbar15.htm#38">במדבר טו' לח'</a>) וְהָיָה כֹּל אֲשֶׁר נָגְעוּ-בוֹ וְנוֹשָׁעוּ׃
</p>
